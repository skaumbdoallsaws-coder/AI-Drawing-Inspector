        /// <summary>
        /// Extract max stress, max displacement, and reaction forces from ICWResults.
        /// </summary>
        private void ExtractResultExtremes(ICWResults results, FeaResultsSummary summary)
        {
            // Get max von Mises stress
            // GetMinMaxStress(NComponent, NElementNumber, NStepNum, DispPlane, NUnits, out ErrorCode)
            // NComponent: swsStressComponentVON = 9
            // NElementNumber: 0 = all elements
            // NStepNum: 0 for static (last step)
            // DispPlane: null
            // NUnits: 0 = N/m^2 (Pa)
            try
            {
                int errCode = 0;
                object stressResult = results.GetMinMaxStress(
                    (int)swsStressComponent_e.swsStressComponentVON,
                    0,  // all elements
                    0,  // step number (0 for static)
                    null, // ref plane (null = global)
                    0,  // units: 0 = N/m^2 (Pa)
                    out errCode);

                LogConnectivityArrayDiagnostics("GetMinMaxStress(VON)", stressResult, errCode);
                if (errCode == 0 && stressResult != null)
                {
                    double[] stressValues = ConvertToDoubleArray(stressResult);
                    // Observed COSMOSWorks layout for static studies (length 4):
                    //   [minNode, minValue, maxNode, maxValue]  (Pa)
                    // The two value slots are odd-indexed; the even-indexed slots are
                    // 1-based node IDs and must NOT be treated as values.
                    if (stressValues != null && stressValues.Length >= 4)
                    {
                        summary.MinVonMisesMpa = stressValues[1] / 1e6;
                        summary.MaxVonMisesMpa = stressValues[3] / 1e6;
                    }
                    else if (stressValues != null && stressValues.Length >= 2)
                    {
                        // Older 2-slot layout: [min, max]
                        summary.MinVonMisesMpa = stressValues[0] / 1e6;
                        summary.MaxVonMisesMpa = stressValues[1] / 1e6;
                    }
                }
                else
                {
                    Warn(summary, $"GetMinMaxStress returned error code {errCode}");
                }
            }
            catch (Exception ex)
            {
                Warn(summary, "Could not get max von Mises: " + ex.Message);
            }

            // Get max displacement
            // GetMinMaxDisplacement(NComponent, NStepNumber, DispPlane, NUnits, out ErrorCode)
            try
            {
                int errCode = 0;
                object dispResult = results.GetMinMaxDisplacement(
                    (int)swsDisplacementComponent_e.swsDisplacementComponentURES,
                    0,  // step number
                    null, // ref plane
                    (int)swsLinearUnit_e.swsLinearUnitMeters,
                    out errCode);

                LogConnectivityArrayDiagnostics("GetMinMaxDisplacement(URES)", dispResult, errCode);
                if (errCode == 0 && dispResult != null)
                {
                    double[] dispValues = ConvertToDoubleArray(dispResult);
                    // Layout matches GetMinMaxStress: [minNode, minValue, maxNode, maxValue]
                    // for length-4 arrays. Slot [3] is the max value in meters.
                    if (dispValues != null && dispValues.Length >= 4)
                    {
                        summary.MaxDisplacementMm = dispValues[3] * 1000.0;
                    }
                    else if (dispValues != null && dispValues.Length >= 2)
                    {
                        summary.MaxDisplacementMm = dispValues[1] * 1000.0;
                    }
                }
                else
                {
                    Warn(summary, $"GetMinMaxDisplacement returned error code {errCode}");
                }
            }
            catch (Exception ex)
            {
                Warn(summary, "Could not get max displacement: " + ex.Message);
            }

            // Get reaction forces
            // GetReactionForcesAndMoments(NStepNumber, DispPlane, NUnits, out ErrorCode)
            try
            {
                int errCode = 0;
                object reactionResult = results.GetReactionForcesAndMoments(
                    0,  // step number
                    null, // ref plane
                    (int)swsForceUnit_e.swsForceUnitNOrNm,
                    out errCode);

                LogConnectivityArrayDiagnostics("GetReactionForcesAndMoments", reactionResult, errCode);
                if (errCode == 0 && reactionResult != null)
                {
                    double[] reactionValues = ConvertToDoubleArray(reactionResult);
                    if (reactionValues == null) reactionValues = new double[0];
                    // COSMOSWorks observed layout: per-node array of size 9 * nodeCount
                    // (~165k entries for 18k nodes), NOT a 6-entry global resultant.
                    // The first six entries are NOT [Fx, Fy, Fz, Mx, My, Mz] of the
                    // global reaction. Refuse to populate from this shape and let the
                    // .OUT solver-text fallback fill in the global resultant instead.
                    if (reactionValues.Length > 0 && reactionValues.Length <= 12)
                    {
                        if (reactionValues.Length >= 3)
                        {
                            summary.ReactionFx = Math.Round(reactionValues[0], 2);
                            summary.ReactionFy = Math.Round(reactionValues[1], 2);
                            summary.ReactionFz = Math.Round(reactionValues[2], 2);
                        }
                        if (reactionValues.Length >= 6)
                        {
                            summary.ReactionMx = Math.Round(reactionValues[3], 6);
                            summary.ReactionMy = Math.Round(reactionValues[4], 6);
                            summary.ReactionMz = Math.Round(reactionValues[5], 6);
                            summary.HasReactionMoments = true;
                        }
                    }
                    else if (reactionValues.Length > 12)
                    {
                        Warn(summary, $"GetReactionForcesAndMoments returned per-node array (length={reactionValues.Length}); deferring to .OUT fallback for global resultant.");
                    }
                }
            }
            catch (Exception ex)
            {
                Warn(summary, "Could not get reaction forces: " + ex.Message);
            }
        }

        /// <summary>
        /// Extract the solver's result mesh (surface triangles with nodal stress and displacement).
        /// Uses ICWMesh to get surface nodes and builds surface triangles from element connectivity.
        /// Maps per-element stress values onto surface nodes for vertex coloring.
        /// </summary>
        private FeaMeshData ExtractResultMesh(dynamic study, IModelDoc2 modelDoc, FeaResultsSummary summary)
        {
            // The installed COSMOSWorks interop does not expose ICWPlot.GetTriangleArray()
            // or GetResult() for direct mesh extraction from plots. Instead, we use the
            // node-based approach: read all nodes/elements from ICWMesh, extract the
            // surface boundary, and map element stress values onto surface nodes.
            return ExtractResultMeshFromNodes(study, modelDoc, summary);
        }

        /// <summary>
        /// Fallback: Extract mesh from the FE node/element data directly.
        /// This path is used when ICWPlot.GetTriangleArray() is not available.
        /// It reads all nodes and elements, extracts the surface boundary, then maps
        /// nodal results onto surface vertices.
        /// </summary>
        private FeaMeshData ExtractResultMeshFromNodes(dynamic study, IModelDoc2 modelDoc, FeaResultsSummary summary)
        {
            Console.WriteLine("    FEA: Using node-based mesh extraction...");

            ICWMesh mesh = null;
            ICWResults results = null;
            try
            {
                mesh = (ICWMesh)study.Mesh;
                results = (ICWResults)study.Results;

                if (mesh == null)
                {
                    Console.WriteLine("    FEA Error: No mesh object available");
                    return null;
                }

                int nodeCount = ((dynamic)mesh).NodeCount;
                int elemCount = ((dynamic)mesh).ElementCount;
                Console.WriteLine($"    FEA: FE mesh: {nodeCount:N0} nodes, {elemCount:N0} elements");

                if (nodeCount == 0 || elemCount == 0)
                {
                    Console.WriteLine("    FEA Error: Empty mesh");
                    return null;
                }

                // Read all node positions using GetNodeLocation (1-based node IDs)
                // GetNodeLocation(int NNodeNo, out double XVal, out double YVal, out double ZVal) -> int
                double[] nodeX = new double[nodeCount + 1]; // +1 for 1-based indexing
                double[] nodeY = new double[nodeCount + 1];
                double[] nodeZ = new double[nodeCount + 1];

                for (int i = 1; i <= nodeCount; i++)
                {
                    try
                    {
                        double xVal = 0, yVal = 0, zVal = 0;
                        int locErr = mesh.GetNodeLocation(i, out xVal, out yVal, out zVal);
                        if (locErr == 0)
                        {
                            nodeX[i] = xVal;
                            nodeY[i] = yVal;
                            nodeZ[i] = zVal;
                        }
                    }
                    catch { /* skip node */ }
                }

                // Read element connectivity and find surface faces
                // GetConnectivity returns all element connectivity as a flat array
                // For tet4: each element has 4 nodes, forming 4 triangular faces
                // A face that appears in exactly one element is on the surface boundary
                Console.WriteLine("    FEA: Extracting surface boundary from tet mesh...");
                var faceCount = new Dictionary<long, int>();  // canonical face key -> count
                var faceNodes = new Dictionary<long, int[]>(); // canonical face key -> [n0, n1, n2]
                // Parent-element index per surface-candidate face. Used in
                // Stage 2 to map per-element stress back onto surface tris for
                // area-weighted projection (smoother contours than the legacy
                // unweighted nodal accumulation).
                var faceParentElem = new Dictionary<long, int>();

                // Try bulk connectivity first, fall back to per-element
                int connectErr = 0;
                object connectData = mesh.GetConnectivity(out connectErr);
                LogConnectivityArrayDiagnostics("GetConnectivity", connectData, connectErr);
                int[] allConnectivity = ConvertToIntArray(connectData);

                if (allConnectivity != null && allConnectivity.Length >= 4)
                {
                    int stride; int offset; int nodesPerElem;
                    DetectConnectivityLayout(allConnectivity, out nodesPerElem, out stride, out offset);
                    Console.WriteLine($"    FEA: Connectivity layout: nodesPerElem={nodesPerElem}, stride={stride}, offset={offset}");
                    AppendFacesFromConnectivity(allConnectivity, nodesPerElem, stride, offset, faceCount, faceNodes, faceParentElem);
                }
                else
                {
                    // Fallback: use GetElements() bulk array
                    object elemBulk = mesh.GetElements();
                    LogConnectivityArrayDiagnostics("GetElements", elemBulk, 0);
                    int[] elemArray = ConvertToIntArray(elemBulk);
                    if (elemArray == null || elemArray.Length < 4)
                    {
                        PopulateSummaryFromSolverOut(modelDoc, summary, nodeX, nodeY, nodeZ);
                        Warn(summary, "FE element connectivity is unavailable; refusing to emit a standard FEA GLB because no solver-backed visualization mesh can be built.");
                        Console.WriteLine("    FEA Error: Could not read element connectivity for solver-backed GLB");
                        return null;
                    }

                    int stride; int offset; int nodesPerElem;
                    DetectConnectivityLayout(elemArray, out nodesPerElem, out stride, out offset);
                    Console.WriteLine($"    FEA: GetElements layout: nodesPerElem={nodesPerElem}, stride={stride}, offset={offset}");
                    AppendFacesFromConnectivity(elemArray, nodesPerElem, stride, offset, faceCount, faceNodes, faceParentElem);
                    // Surface (header=3) tris are also the per-element shell IDs needed
                    // for the stress-fallback loop below; preserve the array reference.
                    allConnectivity = elemArray;
                }

                // Also try GetSurfaceNodesAndNormals for identifying surface node set
                HashSet<int> surfaceNodeHint = null;
                try
                {
                    object varNodeIDs = null, varNumNormals = null, varNormalVecs = null;
                    int surfErr = mesh.GetSurfaceNodesAndNormals(out varNodeIDs, out varNumNormals, out varNormalVecs);
                    if (surfErr == 0 && varNodeIDs != null)
                    {
                        int[] surfNodeIds = ConvertToIntArray(varNodeIDs);
                        if (surfNodeIds != null && surfNodeIds.Length > 0)
                        {
                            surfaceNodeHint = new HashSet<int>(surfNodeIds);
                            Console.WriteLine($"    FEA: GetSurfaceNodesAndNormals returned {surfaceNodeHint.Count:N0} surface nodes");
                        }
                    }
                }
                catch { /* surface node hint is optional */ }

                if (faceCount.Count == 0)
                {
                    Console.WriteLine("    FEA Error: No elements processed");
                    return null;
                }

                // Surface faces appear exactly once
                var surfaceFaces = new List<int[]>();
                // Parallel array: parent element index per surface tri, used by
                // the area-weighted stress projection below. Index `-1` means
                // unknown (parent wasn't tracked).
                var surfaceFaceParents = new List<int>();
                foreach (var kvp in faceCount)
                {
                    if (kvp.Value == 1)
                    {
                        surfaceFaces.Add(faceNodes[kvp.Key]);
                        int parent;
                        surfaceFaceParents.Add(faceParentElem.TryGetValue(kvp.Key, out parent) ? parent : -1);
                    }
                }

                Console.WriteLine($"    FEA: Found {surfaceFaces.Count:N0} surface faces");

                if (surfaceFaces.Count == 0)
                {
                    Console.WriteLine("    FEA Error: No surface faces found");
                    return null;
                }

                // Collect unique surface node IDs and build index remapping
                var surfaceNodeSet = new HashSet<int>();
                foreach (var face in surfaceFaces)
                {
                    surfaceNodeSet.Add(face[0]);
                    surfaceNodeSet.Add(face[1]);
                    surfaceNodeSet.Add(face[2]);
                }

                var surfaceNodes = new List<int>(surfaceNodeSet);
                surfaceNodes.Sort();
                var nodeRemap = new Dictionary<int, int>();
                for (int i = 0; i < surfaceNodes.Count; i++)
                    nodeRemap[surfaceNodes[i]] = i;

                int surfVertCount = surfaceNodes.Count;
                Console.WriteLine($"    FEA: {surfVertCount:N0} surface nodes ({(100.0 * surfVertCount / nodeCount):F1}% of total)");

                // Build position array for surface nodes
                float[] posArray = new float[surfVertCount * 3];
                for (int i = 0; i < surfVertCount; i++)
                {
                    int nid = surfaceNodes[i];
                    posArray[i * 3 + 0] = (float)nodeX[nid];
                    posArray[i * 3 + 1] = (float)nodeY[nid];
                    posArray[i * 3 + 2] = (float)nodeZ[nid];
                }

                // Build index array
                uint[] indices = new uint[surfaceFaces.Count * 3];
                for (int i = 0; i < surfaceFaces.Count; i++)
                {
                    indices[i * 3 + 0] = (uint)nodeRemap[surfaceFaces[i][0]];
                    indices[i * 3 + 1] = (uint)nodeRemap[surfaceFaces[i][1]];
                    indices[i * 3 + 2] = (uint)nodeRemap[surfaceFaces[i][2]];
                }

                // Compute normals from face geometry
                float[] normArray = ComputeVertexNormals(posArray, indices, surfVertCount);

                // Get nodal von Mises stress for surface nodes
                double[] stressValues = new double[surfVertCount];
                // Coverage bitmap. True iff the corresponding surface node
                // received a value from any path. Replaces the older
                // `stressValues[i] != 0` heuristic, which incorrectly treated
                // legitimate zero-stress nodes (constrained corners,
                // unloaded regions) as "missing" and overwrote their values.
                bool[] covered = new bool[surfVertCount];
                int totalCovered = 0;
                double maxStress = 0;
                int peakIdx = 0;

                // Per-path contribution counters so the manifest reports
                // honest provenance (which path supplied which surface
                // nodes), not just a flag based on whether the bulk call
                // returned a non-empty source map.
                int contribPrimary = 0;
                int contribPerNodeBulk = 0;
                int contribPerNodeApi = 0;
                int contribPerElemSurface = 0;

                // Stage 2 PRIMARY path: bulk per-element VON + area-weighted
                // projection to surface nodes. This is the closest equivalent
                // to SolidWorks' "averaged nodal stress" display.
                //
                // Keep perElemVon visible to the CAD-projection stage below so
                // the visualization-side path can project element values
                // directly onto the refined display mesh (avoiding a second
                // round of node-level smoothing that diffuses the contours).
                int stressBulkElemSourcedCount = 0;
                Dictionary<int, double> perElemVon = null;
                if (results != null && surfaceFaces.Count > 0)
                {
                    perElemVon = TryBulkPerElementVon(results);
                    if (perElemVon != null && perElemVon.Count > 0)
                    {
                        stressBulkElemSourcedCount = perElemVon.Count;
                        contribPrimary = ProjectElementStressAreaWeighted(
                            perElemVon, surfaceFaces, surfaceFaceParents,
                            surfaceNodes, nodeRemap,
                            nodeX, nodeY, nodeZ,
                            stressValues, covered, ref maxStress, ref peakIdx);
                        totalCovered += contribPrimary;
                        double pct = surfVertCount > 0 ? 100.0 * totalCovered / surfVertCount : 0;
                        Console.WriteLine($"    FEA: bulk per-element + area-weighted projection covered {contribPrimary:N0}/{surfVertCount:N0} surface nodes (cumulative {pct:F1}%)");
                    }
                }

                // Only invoke the legacy per-node fallback chain if the area-
                // weighted projection above did not achieve good coverage. The
                // 95% threshold lets a small number of corner-case nodes
                // (e.g. degenerate-area surface tris) still get filled by the
                // per-node API without burning the full budget on a path that
                // would mostly duplicate work.
                bool needSecondaryStress = totalCovered < (int)Math.Round(0.95 * surfVertCount);
                if (results != null && needSecondaryStress)
                {
                    try
                    {
                        // Per-node bulk fill: fills gaps left by the area-
                        // weighted primary path. If the primary covered nothing
                        // (e.g. bulk per-element API unsupported), this becomes
                        // the main source.
                        try
                        {
                            int bulkErr = 0;
                            object bulkStress = results.GetStressForEntities2(
                                true,                                                       // BValueByNode
                                (int)swsStressComponent_e.swsStressComponentVON,            // VON
                                0,                                                          // step
                                null,                                                       // DispPlane
                                null,                                                       // ArraySelectedEntities -> "all"
                                0,                                                          // units: Pa
                                out bulkErr);
                            LogConnectivityArrayDiagnostics("GetStressForEntities2(all,VON,byNode)", bulkStress, bulkErr);
                            if (bulkErr == 0 && bulkStress != null)
                            {
                                double[] perEntity = ConvertToDoubleArray(bulkStress);
                                if (perEntity != null && perEntity.Length >= 2)
                                {
                                    // Common COSMOSWorks layout for per-node stress is
                                    // [nodeId, value, nodeId, value, ...]. Build a map.
                                    var nodeVon = new Dictionary<int, double>();
                                    for (int k = 0; k + 1 < perEntity.Length; k += 2)
                                    {
                                        int nid = (int)Math.Round(perEntity[k]);
                                        if (nid > 0) nodeVon[nid] = perEntity[k + 1];
                                    }
                                    int filled = 0;
                                    for (int i = 0; i < surfVertCount; i++)
                                    {
                                        // Gap-fill only -- skip nodes already
                                        // covered by the primary area-weighted
                                        // projection. Coverage tracked via
                                        // explicit boolean so a legitimate
                                        // 0.0 von Mises value is not treated
                                        // as missing.
                                        if (covered[i]) continue;
                                        int nid = surfaceNodes[i];
                                        double v;
                                        if (nodeVon.TryGetValue(nid, out v))
                                        {
                                            stressValues[i] = v;
                                            covered[i] = true;
                                            if (Math.Abs(v) > maxStress) { maxStress = Math.Abs(v); peakIdx = i; }
                                            filled++;
                                        }
                                    }
                                    contribPerNodeBulk = filled;
                                    totalCovered += filled;
                                    Console.WriteLine($"    FEA: Nodal stress (bulk per-node fill): filled={filled:N0}/{surfVertCount:N0} gaps; total covered={totalCovered:N0}");
                                }
                            }
                        }
                        catch (Exception bulkEx)
                        {
                            Console.WriteLine($"    FEA: GetStressForEntities2 bulk attempt threw: {bulkEx.Message}");
                        }

                        // Per-node API fallback: runs whenever bulk paths
                        // haven't reached full coverage. Per-node calls are
                        // slow (one COM round-trip per surface node) so this
                        // path is only worth running when the cheaper bulk
                        // paths actually left gaps.
                        if (totalCovered < surfVertCount)
                        {
                            int failed = 0;
                            int firstErr = 0;
                            string firstErrText = null;
                            bool diagPrinted = false;

                            int filledHere = 0;
                            for (int i = 0; i < surfVertCount; i++)
                            {
                                // Gap-fill only -- skip nodes already covered.
                                if (covered[i]) continue;
                                int nid = surfaceNodes[i];
                                try
                                {
                                    int sErr = 0;
                                    object nodalStress = results.GetStressComponentForAllStepsAtNode(
                                        (int)swsStressComponent_e.swsStressComponentVON,
                                        nid,
                                        null,
                                        0,
                                        out sErr);

                                    if (!diagPrinted)
                                    {
                                        diagPrinted = true;
                                        LogConnectivityArrayDiagnostics(
                                            $"GetStressComponentForAllStepsAtNode(node={nid})",
                                            nodalStress, sErr);
                                    }

                                    if (sErr == 0 && nodalStress != null)
                                    {
                                        double[] perStep = ConvertToDoubleArray(nodalStress);
                                        if (perStep != null && perStep.Length > 0)
                                        {
                                            double von = perStep[perStep.Length - 1];
                                            stressValues[i] = von;
                                            covered[i] = true;
                                            if (Math.Abs(von) > maxStress) { maxStress = Math.Abs(von); peakIdx = i; }
                                            filledHere++;
                                        }
                                        else { failed++; if (firstErr == 0) firstErr = -1001; }
                                    }
                                    else
                                    {
                                        failed++;
                                        if (firstErr == 0) firstErr = sErr;
                                    }
                                }
                                catch (Exception ex)
                                {
                                    failed++;
                                    if (firstErrText == null) firstErrText = ex.Message;
                                }
                            }
                            contribPerNodeApi = filledHere;
                            totalCovered += filledHere;
                            Console.WriteLine($"    FEA: Nodal stress (per-node fill): filled={filledHere:N0} gaps; total covered={totalCovered:N0}/{surfVertCount:N0}, failed={failed}, firstErrCode={firstErr}, firstErrText={firstErrText ?? "(none)"}");
                        }

                        // Per-element-of-surface fallback removed. The previous
                        // implementation looped over 2,379 surface-parent
                        // elements calling GetStress(eid, ...) one at a time at
                        // ~0.65 s/call (~25 min total). On this build of
                        // COSMOSWorks the NElementNumber argument is ignored —
                        // every call returns the entire mesh's stress as a
                        // single flat array of length elementCount × 12. So
                        // the loop wasted ~24:59 of those 25 minutes and read
                        // sVals[9] (elem 1's P3, ~14 MPa) on every call,
                        // producing a constant-stress field that rendered as
                        // solid red. One COM round-trip is sufficient — that
                        // call now lives inside TryBulkPerElementVon as Path B
                        // and lights up the primary "bulk-per-element-area-
                        // weighted" path.
                        // The --fea-stress-budget-seconds /
                        // -StressBudgetSeconds knob is preserved as a no-op
                        // for backward compatibility with worker scripts that
                        // still pass it; nothing reads _stressFallbackBudgetSeconds
                        // anymore on the live code path.
                    }
                    catch (Exception ex)
                    {
                        Warn(summary, "Error reading nodal stress: " + ex.Message);
                    }
                }

                // Update max stress location based on per-vertex stress.
                // NB: do NOT overwrite summary.MaxVonMisesMpa if it's already populated
                // from GetMinMaxStress (slot [3]) — that value covers the entire FE
                // mesh and is more authoritative than partial per-vertex coverage from
                // a budget-capped per-element fallback. Only promote if the per-vertex
                // peak exceeds the current value.
                if (maxStress > 0 && peakIdx < surfVertCount)
                {
                    int nid = surfaceNodes[peakIdx];
                    summary.MaxStressLocation = new double[] { nodeX[nid], nodeY[nid], nodeZ[nid] };
                    double maxMpa = maxStress / 1e6;
                    if (maxMpa > summary.MaxVonMisesMpa)
                        summary.MaxVonMisesMpa = maxMpa;
                    summary.MaxStressNode = nid;  // 1-based SolidWorks node ID
                }

                // Stress-projection diagnostic. PrimaryPath is whichever
                // path contributed the most surface-node coverage, NOT just
                // whichever source map happened to be non-empty -- that was
                // the previous bug where bulk-per-element with mismatched
                // EID convention would yield a non-empty map but project
                // zero nodes.
                string primaryPathLabel;
                int bestContrib = 0;
                primaryPathLabel = "none";
                if (contribPrimary > bestContrib) { bestContrib = contribPrimary; primaryPathLabel = "bulk-per-element-area-weighted"; }
                if (contribPerNodeBulk > bestContrib) { bestContrib = contribPerNodeBulk; primaryPathLabel = "bulk-per-node-fill"; }
                if (contribPerNodeApi > bestContrib) { bestContrib = contribPerNodeApi; primaryPathLabel = "per-node-api"; }
                if (contribPerElemSurface > bestContrib) { bestContrib = contribPerElemSurface; primaryPathLabel = "per-element-of-surface"; }
                summary.StressDiagnostic = new StressDiagnosticInfo
                {
                    SurfaceNodesTotal = surfVertCount,
                    SurfaceNodesCovered = totalCovered,
                    BulkPerElemSourceCount = stressBulkElemSourcedCount,
                    PrimaryPath = primaryPathLabel,
                    ContribBulkPerElement = contribPrimary,
                    ContribBulkPerNode = contribPerNodeBulk,
                    ContribPerNodeApi = contribPerNodeApi,
                    ContribPerElementOfSurface = contribPerElemSurface,
                };

                // Map stress to vertex colors. Use solver-reported [min, max]
                // VON when populated (matches the SolidWorks legend); fall back
                // to [0, peak-from-vertices] when the solver call was
                // unavailable so the FE-mesh path is still self-consistent.
                double colorMinPa = (summary.MinVonMisesMpa > 0 && summary.MaxVonMisesMpa > 0)
                    ? summary.MinVonMisesMpa * 1e6
                    : 0.0;
                double colorMaxPa = (summary.MaxVonMisesMpa > 0)
                    ? summary.MaxVonMisesMpa * 1e6
                    : (maxStress > 0 ? maxStress : 1.0);
                byte[] vertexColors = MapStressToColors(stressValues, surfVertCount, colorMinPa, colorMaxPa);

                // Get nodal displacements for morph target
                float[] morphPositions = null;
                float[] morphNormals = null;
                float[] morphBoundsMin = null;
                float[] morphBoundsMax = null;
                // Lifted to outer scope so the Stage 3 CAD path can reuse
                // the per-FE-vertex displacement arrays (one source of truth
                // per study) without re-parsing the bulk buffer.
                double[] dispXArrOuter = new double[surfVertCount];
                double[] dispYArrOuter = new double[surfVertCount];
                double[] dispZArrOuter = new double[surfVertCount];
                // Per-FE-vertex displacement coverage. The CAD projection
                // path uses this so it does not weight an uncovered (default
                // zero) FE node into the inverse-distance average -- which
                // would synthesize a "no displacement" reading from data
                // that was never solved.
                bool[] dispCoveredOuter = new bool[surfVertCount];
                bool feHasDisp = false;

                try
                {
                    Dictionary<int, double[]> dispByNid = null;
                    string dispLayoutNote = null;
                    // True iff the displacement source is inherently trustworthy:
                    //   - bulk parser cross-checked against (maxNode, maxValue), OR
                    //   - per-node fallback (direct API per-vertex reads).
                    // Length-only bulk acceptance is NOT trustworthy on its own.
                    bool dispSourceValidated = false;

                    // GetTranslationalDisplacement bulk path. The COSMOSWorks API in
                    // this build returns interleaved (nodeId, value) pairs even for
                    // GetMinMaxStress / GetMinMaxDisplacement -- so the bulk
                    // displacement array is NOT necessarily a flat [UX,UY,UZ,...]
                    // sequence. Detect the layout by length, then cross-validate
                    // against (maxNode, maxValue) from GetMinMaxDisplacement.
                    try
                    {
                        int dispErrCode = 0;
                        object dispBulk = results.GetTranslationalDisplacement(
                            0, // step
                            null, // ref plane
                            (int)swsLinearUnit_e.swsLinearUnitMeters,
                            out dispErrCode);

                        if (dispErrCode == 0 && dispBulk != null)
                        {
                            double[] allDisp = ConvertToDoubleArray(dispBulk);
                            if (allDisp != null && allDisp.Length > 0)
                            {
                                Console.WriteLine($"    FEA: GetTranslationalDisplacement returned {allDisp.Length:N0} doubles for {nodeCount:N0} nodes (ratio {(double)allDisp.Length / nodeCount:F2})");
                                bool bulkValidated;
                                dispByNid = ParseBulkDisplacement(allDisp, nodeCount, results, out dispLayoutNote, out bulkValidated);
                                if (dispByNid != null && bulkValidated) dispSourceValidated = true;
                            }
                        }
                        else
                        {
                            Warn(summary, $"GetTranslationalDisplacement returned errCode={dispErrCode}");
                        }
                    }
                    catch (Exception dispEx)
                    {
                        Warn(summary, "GetTranslationalDisplacement failed: " + dispEx.Message);
                    }

                    // Per-node fallback if bulk parse couldn't produce a usable map.
                    // Per-node API reads are trustworthy by construction (no layout
                    // ambiguity), so we mark the source validated even though we
                    // didn't run the bulk cross-check on it.
                    if (dispByNid == null || dispByNid.Count == 0)
                    {
                        dispByNid = ReadDisplacementPerNode(results, surfaceNodes, summary);
                        if (dispByNid != null && dispByNid.Count > 0)
                        {
                            dispLayoutNote = "per-node-fallback";
                            dispSourceValidated = true;
                        }
                    }

                    // Use the outer-scope arrays so the Stage 3 CAD path can
                    // see per-vertex displacements after this try-block exits.
                    double[] dispXArr = dispXArrOuter;
                    double[] dispYArr = dispYArrOuter;
                    double[] dispZArr = dispZArrOuter;
                    bool hasDisp = false;
                    double bestMag = 0;
                    int bestNid = 0;

                    if (dispByNid != null)
                    {
                        for (int i = 0; i < surfVertCount; i++)
                        {
                            int nid = surfaceNodes[i];
                            double[] uvw;
                            if (dispByNid.TryGetValue(nid, out uvw))
                            {
                                dispXArr[i] = uvw[0];
                                dispYArr[i] = uvw[1];
                                dispZArr[i] = uvw[2];
                                dispCoveredOuter[i] = true;
                                hasDisp = true;
                                double mag = Math.Sqrt(uvw[0] * uvw[0] + uvw[1] * uvw[1] + uvw[2] * uvw[2]);
                                if (mag > bestMag) { bestMag = mag; bestNid = nid; }
                            }
                        }
                    }

                    if (hasDisp)
                    {
                        feHasDisp = true; // Visible to the Stage 3 CAD path below.

                        // Trusted-signal gate. Before we bake the morph buffer we
                        // require AT LEAST ONE of:
                        //   (1) the bulk parser cross-checked the chosen stride
                        //       against (maxNode, maxValue) -- dispSourceValidated
                        //   (2) summary.MaxDisplacementMm > 1e-9 (an authoritative
                        //       value already populated from GetMinMaxDisplacement
                        //       in ExtractStudyMetadata, OR from .OUT below)
                        // If neither is available, attempt the .OUT fallback
                        // EARLY so we have an independent reference. If that also
                        // fails, refuse to write a morph that has no way to be
                        // validated -- conservative is the right bar here.
                        double bestMagMm = bestMag * 1000.0;
                        double reportedMaxMm = summary.MaxDisplacementMm;
                        if (!dispSourceValidated && reportedMaxMm <= 1e-9)
                        {
                            Console.WriteLine("    FEA: morph-validation gate has no API or summary signal; attempting .OUT fallback before sanity check.");
                            try { PopulateSummaryFromSolverOut(modelDoc, summary, nodeX, nodeY, nodeZ); }
                            catch (Exception outEx) { Warn(summary, "Early .OUT fallback for morph validation failed: " + outEx.Message); }
                            reportedMaxMm = summary.MaxDisplacementMm;
                        }

                        bool morphInconsistent = false;
                        if (reportedMaxMm > 1e-9 && bestMagMm > 0)
                        {
                            // Have a trusted reference: enforce ratio gate.
                            double ratio = bestMagMm / reportedMaxMm;
                            if (ratio > 10.0 || ratio < 0.1)
                            {
                                morphInconsistent = true;
                                Warn(summary,
                                    $"Morph delta sanity check failed: max parsed magnitude {bestMagMm:G6} mm vs reported max {reportedMaxMm:G6} mm " +
                                    $"(ratio {ratio:G4}, layout='{dispLayoutNote ?? "unknown"}'). Refusing to write corrupt morph buffer.");
                            }
                        }
                        else if (dispSourceValidated)
                        {
                            // Parser cross-checked the chosen stride against the
                            // API's own (maxNode, maxValue), even though
                            // summary.MaxDisplacementMm was never populated. The
                            // values themselves came through the validated layout,
                            // so we can ship the morph.
                            // Promote the parsed magnitude into summary so the
                            // results JSON / manifest carry a usable number.
                            summary.MaxDisplacementMm = bestMagMm;
                        }
                        else
                        {
                            // No bulk validation, no summary signal, no .OUT -- refuse.
                            morphInconsistent = true;
                            Warn(summary,
                                $"Morph buffer rejected: bulk parser used length-only acceptance (layout='{dispLayoutNote ?? "unknown"}') and no GetMinMaxDisplacement / .OUT cross-check value is available to validate the data. Re-run when result database is fully populated.");
                        }

                        if (!morphInconsistent)
                        {
                            if (bestNid > 0) summary.MaxDisplacementNode = bestNid;
                            if (bestMagMm > summary.MaxDisplacementMm)
                                summary.MaxDisplacementMm = bestMagMm;

                            // Morph target stores DELTA positions (deformed - undeformed)
                            morphPositions = new float[surfVertCount * 3];
                            float mMinX = float.MaxValue, mMinY = float.MaxValue, mMinZ = float.MaxValue;
                            float mMaxX = float.MinValue, mMaxY = float.MinValue, mMaxZ = float.MinValue;

                            for (int i = 0; i < surfVertCount; i++)
                            {
                                float dx = (float)dispXArr[i];
                                float dy = (float)dispYArr[i];
                                float dz = (float)dispZArr[i];
                                morphPositions[i * 3 + 0] = dx;
                                morphPositions[i * 3 + 1] = dy;
                                morphPositions[i * 3 + 2] = dz;

                                if (dx < mMinX) mMinX = dx; if (dx > mMaxX) mMaxX = dx;
                                if (dy < mMinY) mMinY = dy; if (dy > mMaxY) mMaxY = dy;
                                if (dz < mMinZ) mMinZ = dz; if (dz > mMaxZ) mMaxZ = dz;
                            }

                            morphBoundsMin = new float[] { mMinX, mMinY, mMinZ };
                            morphBoundsMax = new float[] { mMaxX, mMaxY, mMaxZ };

                            // Record diagnostic in summary so the manifest carries it
                            summary.MorphDiagnostic = new MorphDiagnosticInfo
                            {
                                MaxAbsDeltaM = bestMag,
                                ReportedMaxMm = reportedMaxMm,
                                LayoutDetected = dispLayoutNote ?? "flat-3stride",
                                Ok = true,
                            };

                            // Compute morph normal deltas
                            float[] deformedPos = new float[surfVertCount * 3];
                            for (int i = 0; i < surfVertCount * 3; i++)
                                deformedPos[i] = posArray[i] + morphPositions[i];

                            float[] deformedNormals = ComputeVertexNormals(deformedPos, indices, surfVertCount);
                            morphNormals = new float[deformedNormals.Length];
                            for (int i = 0; i < deformedNormals.Length; i++)
                                morphNormals[i] = deformedNormals[i] - normArray[i];

                            Console.WriteLine($"    FEA: Displacement morph target extracted (max|delta|={bestMagMm:G4} mm via {dispLayoutNote ?? "flat-3stride"})");
                        }
                        else
                        {
                            // Inconsistent: leave morphPositions/Normals null so the
                            // viewer's own sanity check has nothing to disable.
                            summary.MorphDiagnostic = new MorphDiagnosticInfo
                            {
                                MaxAbsDeltaM = bestMag,
                                ReportedMaxMm = reportedMaxMm,
                                LayoutDetected = dispLayoutNote ?? "unknown",
                                Ok = false,
                            };
                        }
                    }
                    else
                    {
                        Warn(summary, "Displacement extraction produced no usable per-node data; morph target omitted.");
                    }
                }
                catch (Exception ex)
                {
                    Warn(summary, "Could not extract displacement morph: " + ex.Message);
                }

                // Trigger the .OUT solver-text fallback when API values look obviously
                // bogus. Displacement values below 1e-9 mm (picometer scale) are not
                // physical for any load that would produce non-zero stress; they
                // typically indicate the API returned metadata in the wrong slot.
                if (summary.MaxVonMisesMpa <= 0 || summary.MaxDisplacementMm < 1e-9 ||
                    (Math.Abs(summary.ReactionFx) < 1e-12 && Math.Abs(summary.ReactionFy) < 1e-12 && Math.Abs(summary.ReactionFz) < 1e-12))
                {
                    PopulateSummaryFromSolverOut(modelDoc, summary, nodeX, nodeY, nodeZ);
                }

                // Build per-FE-surface-triangle arrays for the Stage 3
                // *sharp* contour path. Each surface triangle carries:
                //   - centroid (m, surface-vertex space) for spatial-index
                //     candidate selection
                //   - parent-element raw VON (Pa) so the sharp field can
                //     read the per-element solver value directly, not the
                //     area-weighted nodal-averaged value
                //
                // Why per-element raw VON for sharp:
                //   * stressValues[] (per-FE-surface-vertex) is built by
                //     area-weighted projection of element VONs, so even at
                //     the peak node it's lower than the per-element peak
                //     (e.g. solver reports max = 800 MPa per element, but
                //     the node's area-weighted average is closer to 600 MPa
                //     because adjacent elements have lower VONs).
                //   * To match the SolidWorks contour reaching yellow/red
                //     at the stress concentration, the sharp path must
                //     expose the per-element peak. The blend keeps it
                //     localised: alpha → 1 only where local spread is
                //     large (i.e. genuinely near a concentration), so the
                //     Voronoi-cell texture of per-element painting can't
                //     bleed into flat regions.
                //
                // perElemVon was lifted out of the Stage-2 try block
                // earlier on this branch's history, so it's accessible
                // here.
                int[] feTriIndices = null;
                float[] feTriCentroids = null;
                double[] feTriVon = null;
                if (surfaceFaces.Count > 0 && nodeRemap != null && posArray != null)
                {
                    var triList = new List<int>(surfaceFaces.Count * 3);
                    var ctList = new List<float>(surfaceFaces.Count * 3);
                    var vonList = new List<double>(surfaceFaces.Count);
                    for (int t = 0; t < surfaceFaces.Count; t++)
                    {
                        int[] tri = surfaceFaces[t];
                        if (tri == null || tri.Length < 3) continue;
                        int v0, v1, v2;
                        if (!nodeRemap.TryGetValue(tri[0], out v0)) continue;
                        if (!nodeRemap.TryGetValue(tri[1], out v1)) continue;
                        if (!nodeRemap.TryGetValue(tri[2], out v2)) continue;
                        if (v0 < 0 || v1 < 0 || v2 < 0) continue;
                        if (v0 * 3 + 2 >= posArray.Length
                            || v1 * 3 + 2 >= posArray.Length
                            || v2 * 3 + 2 >= posArray.Length) continue;
                        // Look up the parent element's raw VON. If
                        // unavailable for this tri (shouldn't happen with
                        // the bulk-per-element path on this study, but be
                        // defensive), drop the tri from the sharp index;
                        // sharp will fall back to bary in the helper.
                        int parent = surfaceFaceParents[t];
                        double von;
                        if (parent < 0 || perElemVon == null
                            || !perElemVon.TryGetValue(parent, out von))
                        {
                            continue;
                        }
                        triList.Add(v0); triList.Add(v1); triList.Add(v2);
                        float cx = (posArray[v0 * 3]     + posArray[v1 * 3]     + posArray[v2 * 3])     / 3f;
                        float cy = (posArray[v0 * 3 + 1] + posArray[v1 * 3 + 1] + posArray[v2 * 3 + 1]) / 3f;
                        float cz = (posArray[v0 * 3 + 2] + posArray[v1 * 3 + 2] + posArray[v2 * 3 + 2]) / 3f;
                        ctList.Add(cx); ctList.Add(cy); ctList.Add(cz);
                        vonList.Add(von);
                    }
                    if (triList.Count > 0)
                    {
                        feTriIndices = triList.ToArray();
                        feTriCentroids = ctList.ToArray();
                        feTriVon = vonList.ToArray();
                    }
                }

                // Stage 3 -- Strategy A: try the CAD display tessellation
                // with FE results projected onto its vertices. Returns null
                // on any failure (no part doc, no bodies, all faces fail to
                // tessellate, FE coverage too low, etc.) and we fall
                // through to the FE-mesh path unchanged.
                FeaMeshData cadProjected = TryBuildCadProjectedFeaMesh(
                    modelDoc,
                    posArray, stressValues,
                    dispXArrOuter, dispYArrOuter, dispZArrOuter, feHasDisp,
                    covered, dispCoveredOuter,
                    feTriIndices, feTriCentroids, feTriVon,
                    summary);
                if (cadProjected != null)
                {
                    return cadProjected;
                }

                // FE-mesh fallback path. Compute bounding box.
                float bbMinX = float.MaxValue, bbMinY = float.MaxValue, bbMinZ = float.MaxValue;
                float bbMaxX = float.MinValue, bbMaxY = float.MinValue, bbMaxZ = float.MinValue;
                for (int i = 0; i < posArray.Length; i += 3)
                {
                    if (posArray[i] < bbMinX) bbMinX = posArray[i];
                    if (posArray[i] > bbMaxX) bbMaxX = posArray[i];
                    if (posArray[i + 1] < bbMinY) bbMinY = posArray[i + 1];
                    if (posArray[i + 1] > bbMaxY) bbMaxY = posArray[i + 1];
                    if (posArray[i + 2] < bbMinZ) bbMinZ = posArray[i + 2];
                    if (posArray[i + 2] > bbMaxZ) bbMaxZ = posArray[i + 2];
                }

                // Display peak on the FE-mesh fallback path: the brightest
                // FE surface vertex. Same semantics as the CAD path's display
                // peak — separate from solver-authoritative peak fields.
                int feDisplayPeakIdx = peakIdx >= 0 && peakIdx < surfVertCount ? peakIdx : -1;
                double feDisplayPeakStressPa = feDisplayPeakIdx >= 0 ? stressValues[feDisplayPeakIdx] : 0.0;
                double[] feDisplayPeakLoc = null;
                if (feDisplayPeakIdx >= 0)
                {
                    int nid = surfaceNodes[feDisplayPeakIdx];
                    feDisplayPeakLoc = new double[] { nodeX[nid], nodeY[nid], nodeZ[nid] };
                }
                string feRangeSource = (summary.MinVonMisesMpa > 0 && summary.MaxVonMisesMpa > 0)
                    ? "solver_minmax"
                    : "observed_max";

                return new FeaMeshData
                {
                    Positions = posArray,
                    Normals = normArray,
                    Indices = indices,
                    VertexColors = vertexColors,
                    MorphPositions = morphPositions,
                    MorphNormals = morphNormals,
                    BoundsMin = new float[] { bbMinX, bbMinY, bbMinZ },
                    BoundsMax = new float[] { bbMaxX, bbMaxY, bbMaxZ },
                    MorphBoundsMin = morphBoundsMin,
                    MorphBoundsMax = morphBoundsMax,
                    VertexCount = surfVertCount,
                    TriangleCount = surfaceFaces.Count,
                    UniqueNodeCount = surfVertCount,
                    // FE-mesh fallback path: the display scalar IS the FE
                    // surface stress array directly (no projection).
                    DisplayVertexStressPa = stressValues,
                    DisplayPeakVertexIndex = feDisplayPeakIdx,
                    DisplayPeakStressPa = feDisplayPeakStressPa,
                    DisplayPeakStressLocationM = feDisplayPeakLoc,
                    DisplayColorRangeMinPa = colorMinPa,
                    DisplayColorRangeMaxPa = colorMaxPa,
                    DisplayColorRangeSource = feRangeSource,
                    DisplayScalarFieldMode = "fe_surface_node_stress",
                    DisplayScalarFieldSource = "per_FE_surface_node_area_weighted_element_average",
                };
            }
            finally
            {
                SafeReleaseCom(results);
                SafeReleaseCom(mesh);
            }
        }

        private void PopulateSummaryFromSolverOut(IModelDoc2 modelDoc, FeaResultsSummary summary, double[] nodeX, double[] nodeY, double[] nodeZ)
        {
            string docPath = null;
            try
            {
                if (modelDoc != null)
                    docPath = modelDoc.GetPathName();
            }
            catch { }
            if (string.IsNullOrWhiteSpace(docPath))
                return;

            string docDir = Path.GetDirectoryName(docPath);
            if (string.IsNullOrWhiteSpace(docDir))
                return;

            string resultsDir = Path.Combine(docDir, "Simulation Results");
            if (!Directory.Exists(resultsDir))
                return;

            string docBase = Path.GetFileNameWithoutExtension(docPath);
            string studyName = summary != null ? (summary.StudyName ?? "") : "";
            string outPath = Path.Combine(resultsDir, docBase + "-" + studyName + ".OUT");
            if (!File.Exists(outPath))
            {
                string safeStudyPattern = studyName.Replace("*", "_").Replace("?", "_");
                string[] matches = Directory.GetFiles(resultsDir, "*" + safeStudyPattern + "*.OUT");
                if (matches.Length > 0)
                    outPath = matches[0];
            }
            if (!File.Exists(outPath))
                return;

            bool populated = false;
            try
            {
                string text = File.ReadAllText(outPath);
                Match stress = Regex.Match(text, @"MAXIMUM\s+NODAL\s+VON\s+MISES\s+STRESS[\s\S]*?NODE\s+(\d+)\s+MAX\.\s+([+\-]?\d+(?:\.\d+)?(?:E[+\-]?\d+)?)", RegexOptions.IgnoreCase);
                if (stress.Success)
                {
                    int node = int.Parse(stress.Groups[1].Value, CultureInfo.InvariantCulture);
                    double pa = ParseFortranDouble(stress.Groups[2].Value);
                    if (pa > 0)
                    {
                        summary.MaxVonMisesMpa = pa / 1e6;
                        summary.MaxStressNode = node;
                        if (node > 0 && nodeX != null && node < nodeX.Length)
                            summary.MaxStressLocation = new double[] { nodeX[node], nodeY[node], nodeZ[node] };
                        populated = true;
                    }
                }

                Match disp = Regex.Match(text, @"MAXIMUM\s+RESULTANT\s+DISPLACEMENT[\s\S]*?NODE\s+(\d+)\s+MAX\.\s+([+\-]?\d+(?:\.\d+)?(?:E[+\-]?\d+)?)", RegexOptions.IgnoreCase);
                if (disp.Success)
                {
                    int node = int.Parse(disp.Groups[1].Value, CultureInfo.InvariantCulture);
                    double meters = ParseFortranDouble(disp.Groups[2].Value);
                    if (meters > 0)
                    {
                        summary.MaxDisplacementMm = meters * 1000.0;
                        summary.MaxDisplacementNode = node;
                        populated = true;
                    }
                }

                Match react = Regex.Match(text, @"Total\s+React\.\s+([+\-]?\.?\d+(?:\.\d+)?(?:E[+\-]?\d+)?)\s+([+\-]?\.?\d+(?:\.\d+)?(?:E[+\-]?\d+)?)\s+([+\-]?\.?\d+(?:\.\d+)?(?:E[+\-]?\d+)?)\s+([+\-]?\.?\d+(?:\.\d+)?(?:E[+\-]?\d+)?)\s+([+\-]?\.?\d+(?:\.\d+)?(?:E[+\-]?\d+)?)\s+([+\-]?\.?\d+(?:\.\d+)?(?:E[+\-]?\d+)?)", RegexOptions.IgnoreCase);
                if (react.Success)
                {
                    summary.ReactionFx = Math.Round(ParseFortranDouble(react.Groups[1].Value), 2);
                    summary.ReactionFy = Math.Round(ParseFortranDouble(react.Groups[2].Value), 2);
                    summary.ReactionFz = Math.Round(ParseFortranDouble(react.Groups[3].Value), 2);
                    summary.ReactionMx = Math.Round(ParseFortranDouble(react.Groups[4].Value), 6);
                    summary.ReactionMy = Math.Round(ParseFortranDouble(react.Groups[5].Value), 6);
                    summary.ReactionMz = Math.Round(ParseFortranDouble(react.Groups[6].Value), 6);
                    summary.HasReactionMoments = true;
                    populated = true;
                }

                if (populated)
                    Warn(summary, "Numeric result extrema were populated from the SolidWorks solver .OUT file after direct COSMOSWorks result access was incomplete.");
            }
            catch (Exception ex)
            {
                Warn(summary, "Could not parse solver .OUT summary: " + ex.Message);
            }
        }

        private static double ParseFortranDouble(string value)
        {
            if (string.IsNullOrWhiteSpace(value)) return 0;
            value = value.Trim().Replace('D', 'E').Replace('d', 'E');
            double parsed;
            return double.TryParse(value, NumberStyles.Float, CultureInfo.InvariantCulture, out parsed) ? parsed : 0;
        }

        /// <summary>
        /// Map stress values to RGBA vertex colours using the standard FEA heatmap:
        /// Blue (0%) -> Cyan (25%) -> Green (50%) -> Yellow (75%) -> Red (100%).
        ///
        /// Normalisation runs over the explicit [minStress, maxStress] range,
        /// matching the SolidWorks legend convention. The previous mapping was
        /// abs(s)/max which (a) silently took absolute value of an already-
        /// non-negative quantity and (b) anchored at 0 instead of the solver's
        /// reported minimum, pushing low-stress regions toward the middle of
        /// the gradient and producing the "everything is green" wash.
        /// </summary>
        private byte[] MapStressToColors(double[] stressValues, int vertexCount, double minStress, double maxStress)
        {
            byte[] colors = new byte[vertexCount * 4]; // RGBA
            double range = maxStress - minStress;
            // Degenerate range (max == min, or both zero, or unsigned weirdness):
            // every vertex normalises to 0 (blue). Better than dividing by 0
            // and getting NaN-coloured bytes.
            bool degenerate = !(range > 1e-30);

            for (int i = 0; i < vertexCount; i++)
            {
                double normalized;
                if (degenerate)
                {
                    normalized = 0.0;
                }
                else
                {
                    normalized = (stressValues[i] - minStress) / range;
                    if (normalized < 0.0) normalized = 0.0;
                    else if (normalized > 1.0) normalized = 1.0;
                }

                byte r, g, b;
                StressToRgb(normalized, out r, out g, out b);

                colors[i * 4 + 0] = r;
                colors[i * 4 + 1] = g;
                colors[i * 4 + 2] = b;
                colors[i * 4 + 3] = 255;
            }

            return colors;
        }

        /// <summary>
        /// Convert a normalized stress value [0..1] to RGB using a 5-stop gradient:
        ///   0.00 = Blue    (0,   0,   255)
        ///   0.25 = Cyan    (0,   255, 255)
        ///   0.50 = Green   (0,   255, 0)
        ///   0.75 = Yellow  (255, 255, 0)
        ///   1.00 = Red     (255, 0,   0)
        /// </summary>
        private void StressToRgb(double t, out byte r, out byte g, out byte b)
        {
            if (t <= 0.0)
            {
                r = 0; g = 0; b = 255;
            }
            else if (t <= 0.25)
            {
                // Blue -> Cyan: increase green
                double f = t / 0.25;
                r = 0;
                g = (byte)(255 * f);
                b = 255;
            }
            else if (t <= 0.50)
            {
                // Cyan -> Green: decrease blue
                double f = (t - 0.25) / 0.25;
                r = 0;
                g = 255;
                b = (byte)(255 * (1.0 - f));
            }
            else if (t <= 0.75)
            {
                // Green -> Yellow: increase red
                double f = (t - 0.50) / 0.25;
                r = (byte)(255 * f);
                g = 255;
                b = 0;
            }
            else if (t < 1.0)
            {
                // Yellow -> Red: decrease green
                double f = (t - 0.75) / 0.25;
                r = 255;
                g = (byte)(255 * (1.0 - f));
                b = 0;
            }
            else
            {
                r = 255; g = 0; b = 0;
            }
        }

        /// <summary>
        /// Compute face normals for a triangle list (3 vertices per triangle, sequential).
        /// Returns per-vertex normals (flat shading — each triangle's 3 vertices share the face normal).
        /// </summary>
        private float[] ComputeFaceNormals(float[] positions, int vertexCount)
        {
            float[] normals = new float[vertexCount * 3];

            for (int tri = 0; tri < vertexCount / 3; tri++)
            {
                int i0 = tri * 3;
                int i1 = i0 + 1;
                int i2 = i0 + 2;

                float ax = positions[i1 * 3 + 0] - positions[i0 * 3 + 0];
                float ay = positions[i1 * 3 + 1] - positions[i0 * 3 + 1];
                float az = positions[i1 * 3 + 2] - positions[i0 * 3 + 2];

                float bx = positions[i2 * 3 + 0] - positions[i0 * 3 + 0];
                float by = positions[i2 * 3 + 1] - positions[i0 * 3 + 1];
                float bz = positions[i2 * 3 + 2] - positions[i0 * 3 + 2];

                float nx = ay * bz - az * by;
                float ny = az * bx - ax * bz;
                float nz = ax * by - ay * bx;

                float len = (float)Math.Sqrt(nx * nx + ny * ny + nz * nz);
                if (len > 1e-10f)
                {
                    nx /= len; ny /= len; nz /= len;
                }
                else
                {
                    nx = 0f; ny = 1f; nz = 0f;
                }

                // Assign same normal to all 3 vertices of this triangle
                normals[i0 * 3 + 0] = nx; normals[i0 * 3 + 1] = ny; normals[i0 * 3 + 2] = nz;
                normals[i1 * 3 + 0] = nx; normals[i1 * 3 + 1] = ny; normals[i1 * 3 + 2] = nz;
                normals[i2 * 3 + 0] = nx; normals[i2 * 3 + 1] = ny; normals[i2 * 3 + 2] = nz;
            }

            return normals;
        }

        /// <summary>
        /// Compute smooth vertex normals by averaging face normals of adjacent triangles.
        /// Used when mesh has indexed triangles (not sequential triangle list).
        /// </summary>
        private float[] ComputeVertexNormals(float[] positions, uint[] indices, int vertexCount)
        {
            float[] normals = new float[vertexCount * 3];

            // Accumulate face normals at each vertex
            for (int tri = 0; tri < indices.Length / 3; tri++)
            {
                uint i0 = indices[tri * 3 + 0];
                uint i1 = indices[tri * 3 + 1];
                uint i2 = indices[tri * 3 + 2];

                float ax = positions[i1 * 3 + 0] - positions[i0 * 3 + 0];
                float ay = positions[i1 * 3 + 1] - positions[i0 * 3 + 1];
                float az = positions[i1 * 3 + 2] - positions[i0 * 3 + 2];

                float bx = positions[i2 * 3 + 0] - positions[i0 * 3 + 0];
                float by = positions[i2 * 3 + 1] - positions[i0 * 3 + 1];
                float bz = positions[i2 * 3 + 2] - positions[i0 * 3 + 2];

                float nx = ay * bz - az * by;
                float ny = az * bx - ax * bz;
                float nz = ax * by - ay * bx;

                // Don't normalize yet — area-weighted accumulation
                normals[i0 * 3 + 0] += nx; normals[i0 * 3 + 1] += ny; normals[i0 * 3 + 2] += nz;
                normals[i1 * 3 + 0] += nx; normals[i1 * 3 + 1] += ny; normals[i1 * 3 + 2] += nz;
                normals[i2 * 3 + 0] += nx; normals[i2 * 3 + 1] += ny; normals[i2 * 3 + 2] += nz;
            }

            // Normalize all vertex normals
            for (int i = 0; i < vertexCount; i++)
            {
                float nx = normals[i * 3 + 0];
                float ny = normals[i * 3 + 1];
                float nz = normals[i * 3 + 2];
                float len = (float)Math.Sqrt(nx * nx + ny * ny + nz * nz);
                if (len > 1e-10f)
                {
                    normals[i * 3 + 0] = nx / len;
                    normals[i * 3 + 1] = ny / len;
                    normals[i * 3 + 2] = nz / len;
                }
                else
                {
                    normals[i * 3 + 0] = 0f;
                    normals[i * 3 + 1] = 1f;
                    normals[i * 3 + 2] = 0f;
                }
            }

            return normals;
        }

        /// <summary>
        /// Build a canonical key for a triangle face from 3 node IDs.
        /// Sorts the IDs and packs into a single long for fast hashing.
        /// </summary>
        private void AddFace(Dictionary<long, int> faceCount, Dictionary<long, int[]> faceNodes,
            int n0, int n1, int n2,
            int parentElemIdx = -1, Dictionary<long, int> faceParentElem = null)
        {
            // Sort the three node IDs
            int a = n0, b = n1, c = n2;
            if (a > b) { int t = a; a = b; b = t; }
            if (b > c) { int t = b; b = c; c = t; }
            if (a > b) { int t = a; a = b; b = t; }

            // Pack into a long: a * 10^12 + b * 10^6 + c
            // Assumes node IDs < 1,000,000 (safe for typical FEA meshes)
            long key = (long)a * 1000000L * 1000000L + (long)b * 1000000L + (long)c;

            if (faceCount.ContainsKey(key))
            {
                faceCount[key]++;
                // Interior face: more than one element shares it. Stress
                // projection only uses surface faces (count == 1) so the
                // parent of an interior face is irrelevant.
            }
            else
            {
                faceCount[key] = 1;
                faceNodes[key] = new int[] { n0, n1, n2 }; // preserve original winding
                if (faceParentElem != null && parentElemIdx >= 0)
                    faceParentElem[key] = parentElemIdx;
            }
        }

        /// <summary>
        /// Detect connectivity layout from a leading header value.
        /// Observed shapes from COSMOSWorks GetConnectivity / GetElements:
        ///   header=3:  surface tri shells       -> stride=4, offset=1, 3 nodes/elem
        ///   header=4:  tet4 (corners only)      -> stride=5, offset=1
        ///   header=10: tet10 (corners + mid)    -> stride=11, offset=1
        ///   else:      bare tet4 (no header)    -> stride=4, offset=0, assume tet4
        /// </summary>
        private static void DetectConnectivityLayout(int[] connectivity, out int nodesPerElem, out int stride, out int offset)
        {
            int header = connectivity != null && connectivity.Length > 0 ? connectivity[0] : 0;
            switch (header)
            {
                case 3:  nodesPerElem = 3;  stride = 4;  offset = 1; break;
                case 4:  nodesPerElem = 4;  stride = 5;  offset = 1; break;
                case 10: nodesPerElem = 10; stride = 11; offset = 1; break;
                default: nodesPerElem = 4;  stride = 4;  offset = 0; break;
            }
        }

        /// <summary>
        /// Append face entries from a connectivity array. Surface tris (3 nodes/elem) are
        /// added directly as a single face per entry; tet4 / tet10 each contribute four
        /// candidate triangle faces (the tet's four faces) so that AddFace's dedup picks
        /// out the boundary by count==1.
        /// </summary>
        /// <summary>
        /// Project per-element von Mises stress onto surface nodes via
        /// AREA-WEIGHTED averaging. For each surface triangle (a face of the
        /// FE mesh appearing in exactly one element), we compute the tri area
        /// from its corner positions and accumulate `area * vonOfParent` and
        /// `area` to each of its three corner nodes. Final per-node value is
        /// `sum(area * von) / sum(area)`. This replaces the legacy unweighted
        /// `sum(von) / count` accumulation, which produced visibly noisy
        /// contours because tiny shell elements adjacent to large ones got
        /// equal say in the average.
        ///
        /// Returns the number of surface nodes that received at least one
        /// contribution (i.e., coverage). The output `stressValues` is filled
        /// in surface-vertex order; entries that received no contribution
        /// remain at their pre-existing value (so this can fill gaps left by
        /// a partial bulk per-node read).
        /// </summary>
        private int ProjectElementStressAreaWeighted(
            Dictionary<int, double> perElementVon,
            List<int[]> surfaceFaces,
            List<int> surfaceFaceParents,
            List<int> surfaceNodes,
            Dictionary<int, int> nodeRemap,
            double[] nodeX, double[] nodeY, double[] nodeZ,
            double[] stressValues, // surface-vertex-ordered output
            bool[] covered,        // surface-vertex-ordered coverage mask (mutated)
            ref double maxStress,
            ref int peakIdx)
        {
            if (perElementVon == null || perElementVon.Count == 0) return 0;
            if (surfaceFaces == null || surfaceFaces.Count == 0) return 0;

            int surfVertCount = stressValues.Length;
            // Per-vertex accumulators in surface-index order. Vertices that
            // are already covered by a previous path are skipped so this
            // function works as both primary and gap-filler.
            double[] sumAreaVon = new double[surfVertCount];
            double[] sumArea = new double[surfVertCount];

            for (int t = 0; t < surfaceFaces.Count; t++)
            {
                int parent = surfaceFaceParents[t];
                if (parent < 0) continue;
                double von;
                if (!perElementVon.TryGetValue(parent, out von)) continue;
                int[] tri = surfaceFaces[t];
                int n0 = tri[0], n1 = tri[1], n2 = tri[2];
                double area = TriArea(nodeX, nodeY, nodeZ, n0, n1, n2);
                if (area <= 0) continue;

                int v0; if (!nodeRemap.TryGetValue(n0, out v0)) continue;
                int v1; if (!nodeRemap.TryGetValue(n1, out v1)) continue;
                int v2; if (!nodeRemap.TryGetValue(n2, out v2)) continue;

                sumAreaVon[v0] += area * von; sumArea[v0] += area;
                sumAreaVon[v1] += area * von; sumArea[v1] += area;
                sumAreaVon[v2] += area * von; sumArea[v2] += area;
            }

            int newlyCovered = 0;
            for (int i = 0; i < surfVertCount; i++)
            {
                if (covered[i]) continue;        // gap-filler semantics
                if (sumArea[i] <= 0) continue;
                double v = sumAreaVon[i] / sumArea[i];
                stressValues[i] = v;
                covered[i] = true;
                newlyCovered++;
                double abs = Math.Abs(v);
                if (abs > maxStress) { maxStress = abs; peakIdx = i; }
            }
            return newlyCovered;
        }

        private static double TriArea(double[] nodeX, double[] nodeY, double[] nodeZ, int n0, int n1, int n2)
        {
            if (n0 <= 0 || n1 <= 0 || n2 <= 0) return 0;
            if (n0 >= nodeX.Length || n1 >= nodeX.Length || n2 >= nodeX.Length) return 0;
            double ax = nodeX[n1] - nodeX[n0], ay = nodeY[n1] - nodeY[n0], az = nodeZ[n1] - nodeZ[n0];
            double bx = nodeX[n2] - nodeX[n0], by = nodeY[n2] - nodeY[n0], bz = nodeZ[n2] - nodeZ[n0];
            // Cross product magnitude / 2
            double cx = ay * bz - az * by;
            double cy = az * bx - ax * bz;
            double cz = ax * by - ay * bx;
            return 0.5 * Math.Sqrt(cx * cx + cy * cy + cz * cz);
        }

        /// <summary>
        /// Closest point on a triangle to a query point in 3D, returned as
        /// barycentric coordinates (u, v, w) over (A, B, C) with u + v + w = 1.
        /// Also returns the squared distance from the query to the closest
        /// point. Uses the standard 7-Voronoi-region algorithm from Real-Time
        /// Collision Detection (Ericson §5.1.5): tests the three vertex
        /// regions, the three edge regions, and the interior in order, so
        /// the returned bary coords are valid (non-negative, sum to 1)
        /// regardless of whether the projection of the query lands inside
        /// or outside the triangle.
        /// </summary>
        private static void ClosestPointOnTriangle(
            double px, double py, double pz,
            double ax, double ay, double az,
            double bx, double by, double bz,
            double cx, double cy, double cz,
            out double u, out double v, out double w, out double dist2)
        {
            // Edge vectors and AP/BP/CP
            double abx = bx - ax, aby = by - ay, abz = bz - az;
            double acx = cx - ax, acy = cy - ay, acz = cz - az;
            double apx = px - ax, apy = py - ay, apz = pz - az;

            // Region A: P projects outside the triangle through vertex A
            double d1 = abx * apx + aby * apy + abz * apz;
            double d2 = acx * apx + acy * apy + acz * apz;
            if (d1 <= 0 && d2 <= 0)
            {
                u = 1; v = 0; w = 0;
                double dx = px - ax, dy = py - ay, dz = pz - az;
                dist2 = dx * dx + dy * dy + dz * dz;
                return;
            }

            // Region B
            double bpx = px - bx, bpy = py - by, bpz = pz - bz;
            double d3 = abx * bpx + aby * bpy + abz * bpz;
            double d4 = acx * bpx + acy * bpy + acz * bpz;
            if (d3 >= 0 && d4 <= d3)
            {
                u = 0; v = 1; w = 0;
                double dx = px - bx, dy = py - by, dz = pz - bz;
                dist2 = dx * dx + dy * dy + dz * dz;
                return;
            }

            // Region AB edge
            double vc = d1 * d4 - d3 * d2;
            if (vc <= 0 && d1 >= 0 && d3 <= 0)
            {
                double t = d1 / (d1 - d3);
                u = 1 - t; v = t; w = 0;
                double qx = ax + t * abx, qy = ay + t * aby, qz = az + t * abz;
                double dx = px - qx, dy = py - qy, dz = pz - qz;
                dist2 = dx * dx + dy * dy + dz * dz;
                return;
            }

            // Region C
            double cpx = px - cx, cpy = py - cy, cpz = pz - cz;
            double d5 = abx * cpx + aby * cpy + abz * cpz;
            double d6 = acx * cpx + acy * cpy + acz * cpz;
            if (d6 >= 0 && d5 <= d6)
            {
                u = 0; v = 0; w = 1;
                double dx = px - cx, dy = py - cy, dz = pz - cz;
                dist2 = dx * dx + dy * dy + dz * dz;
                return;
            }

            // Region AC edge
            double vb = d5 * d2 - d1 * d6;
            if (vb <= 0 && d2 >= 0 && d6 <= 0)
            {
                double t = d2 / (d2 - d6);
                u = 1 - t; v = 0; w = t;
                double qx = ax + t * acx, qy = ay + t * acy, qz = az + t * acz;
                double dx = px - qx, dy = py - qy, dz = pz - qz;
                dist2 = dx * dx + dy * dy + dz * dz;
                return;
            }

            // Region BC edge
            double va = d3 * d6 - d5 * d4;
            if (va <= 0 && (d4 - d3) >= 0 && (d5 - d6) >= 0)
            {
                double t = (d4 - d3) / ((d4 - d3) + (d5 - d6));
                u = 0; v = 1 - t; w = t;
                double qx = bx + t * (cx - bx);
                double qy = by + t * (cy - by);
                double qz = bz + t * (cz - bz);
                double dx = px - qx, dy = py - qy, dz = pz - qz;
                dist2 = dx * dx + dy * dy + dz * dz;
                return;
            }

            // Interior: P projects strictly inside the triangle
            double denom = va + vb + vc;
            if (denom <= 0)
            {
                // Degenerate triangle (zero area). Fall back to vertex A.
                u = 1; v = 0; w = 0;
                double dx = px - ax, dy = py - ay, dz = pz - az;
                dist2 = dx * dx + dy * dy + dz * dz;
                return;
            }
            double inv = 1.0 / denom;
            v = vb * inv;
            w = vc * inv;
            u = 1 - v - w;
            double qxI = ax + abx * v + acx * w;
            double qyI = ay + aby * v + acy * w;
            double qzI = az + abz * v + acz * w;
            double dxI = px - qxI, dyI = py - qyI, dzI = pz - qzI;
            dist2 = dxI * dxI + dyI * dyI + dzI * dzI;
        }

        /// <summary>
        /// Try the bulk per-element stress API. Returns a (ZERO-based element
        /// index) → von Mises value (Pa) map if either of the two paths
        /// succeeds, null otherwise.
        ///
        /// Path A (preferred — modern COSMOSWorks builds):
        ///   GetStressForEntities2(byElement=false, VON, all=null) returns
        ///   interleaved [eid, value, eid, value, ...] pairs. 1-based EIDs.
        ///
        /// Path B (fallback for older / restricted builds — verified on
        ///   SolidWorks 2024 v32.5.0 worker machine where Path A returns
        ///   DBNull / errCode 7):
        ///   GetStress(NElementNumber=1, ...) ignores NElementNumber and
        ///   returns the entire mesh's stress as a single flat array of
        ///   length elementCount × 12. Per-stride layout (0-based offsets):
        ///     [0]=eid (1-based), [1..6]=SX/SY/SZ/TXY/TXZ/TYZ,
        ///     [7..9]=P1/P2/P3, [10]=VON, [11]=INT.
        ///   Verified empirically — see commit ceebd77 for raw probe output.
        ///
        /// Both paths return 0-based EID keys to match surfaceFaceParents,
        /// which stores the 0-based connectivity loop index. This alignment
        /// is required by ProjectElementStressAreaWeighted — if it diverges,
        /// the projection silently no-ops and produces an empty stress field.
        /// </summary>
        private Dictionary<int, double> TryBulkPerElementVon(ICWResults results)
        {
            // ---------- Path A: GetStressForEntities2 ----------
            try
            {
                int err = 0;
                object bulk = results.GetStressForEntities2(
                    false, // BValueByNode = false → per-element
                    (int)swsStressComponent_e.swsStressComponentVON,
                    0,
                    null,
                    null,
                    0,
                    out err);
                LogConnectivityArrayDiagnostics("GetStressForEntities2(all,VON,byElement)", bulk, err);
                if (err == 0 && bulk != null)
                {
                    double[] flat = ConvertToDoubleArray(bulk);
                    if (flat != null && flat.Length >= 2)
                    {
                        var map = new Dictionary<int, double>(flat.Length / 2);
                        for (int k = 0; k + 1 < flat.Length; k += 2)
                        {
                            int eid = (int)Math.Round(flat[k]);
                            if (eid > 0) map[eid - 1] = flat[k + 1];
                        }
                        if (map.Count > 0)
                        {
                            Console.WriteLine($"    FEA: bulk per-element VON via GetStressForEntities2: {map.Count:N0} elements (raw length {flat.Length:N0}; keys converted to 0-based)");
                            return map;
                        }
                    }
                }
            }
            catch (Exception ex)
            {
                Console.WriteLine($"    FEA: GetStressForEntities2 threw: {ex.Message}");
            }

            // ---------- Path B: GetStress full-mesh fallback ----------
            try
            {
                int err = 0;
                object full = results.GetStress(1, 0, null, 0, out err);
                LogConnectivityArrayDiagnostics("GetStress(1, full-mesh)", full, err);
                if (err != 0 || full == null) return null;
                double[] sVals = ConvertToDoubleArray(full);
                if (sVals == null || sVals.Length < 12 || (sVals.Length % 12) != 0)
                {
                    Console.WriteLine($"    FEA: GetStress full-mesh array length {(sVals == null ? -1 : sVals.Length)} is not a multiple of 12; cannot parse.");
                    return null;
                }
                const int STRIDE = 12;
                const int VON_OFFSET = 10;
                const int EID_OFFSET = 0;
                int strides = sVals.Length / STRIDE;
                var sb = new System.Text.StringBuilder();
                sb.Append("    FEA: GetStress full-mesh first stride: ");
                for (int k = 0; k < STRIDE; k++)
                {
                    if (k > 0) sb.Append(", ");
                    sb.AppendFormat(System.Globalization.CultureInfo.InvariantCulture,
                        "[{0}]={1:E3}", k, sVals[k]);
                }
                Console.WriteLine(sb.ToString());
                Console.WriteLine($"    FEA: GetStress full-mesh array length = {sVals.Length} ({strides:N0} elements × stride 12 = elementCount × [eid, SX, SY, SZ, TXY, TXZ, TYZ, P1, P2, P3, VON, INT]); reading VON at offset {VON_OFFSET}");
                var map = new Dictionary<int, double>(strides);
                int eidMismatches = 0;
                for (int s = 0; s < strides; s++)
                {
                    int baseIdx = s * STRIDE;
                    int eid = (int)Math.Round(sVals[baseIdx + EID_OFFSET]);
                    if (eid <= 0) { eidMismatches++; continue; }
                    map[eid - 1] = sVals[baseIdx + VON_OFFSET];
                }
                Console.WriteLine($"    FEA: bulk per-element VON via GetStress full-mesh: {map.Count:N0} elements (eidMismatches={eidMismatches})");
                return map.Count > 0 ? map : null;
            }
            catch (Exception ex)
            {
                Console.WriteLine($"    FEA: GetStress full-mesh threw: {ex.Message}");
                return null;
            }
        }

        // ====================================================================
        // Stage 3 Strategy A -- CAD-display tessellation with projected results
        // ====================================================================
        //
        // The FE surface mesh has 2-3K vertices for a typical part and follows
        // the FE mesh discretisation (visible facets). SolidWorks' own result
        // viewer uses the CAD display tessellation -- the same tessellation
        // shown in the modelling viewport -- with solver values projected onto
        // its vertices. That gives smoother surfaces (especially around curved
        // features like fillets and holes) at no cost in result accuracy,
        // because the FE results themselves are unchanged; only the surface
        // they're rendered on is finer.
        //
        // The projection is INVERSE-DISTANCE-WEIGHTED nearest-K (K=4): for
        // each CAD vertex, find the 4 nearest FE surface nodes and average
        // their stress / displacement weighted by 1/distance^2. Gives smooth
        // contours and avoids the "binary blue-with-red-spikes" appearance
        // that an FE-mesh-only render produces.

        private class CadDisplayMesh
        {
            public float[] Positions;   // VEC3 floats, length = VertexCount * 3
            public float[] Normals;     // VEC3 floats, length = VertexCount * 3
            public uint[] Indices;      // length = TriangleCount * 3
            public int VertexCount;
            public int TriangleCount;
        }

        /// <summary>
        /// Walk every IFace2 of every solid body in the part and build a
        /// deduplicated CAD display tessellation. Vertex positions are kept
        /// in part-local coordinates (meters), matching the FE node frame so
        /// the projection step doesn't need to apply a transform.
        ///
        /// Returns null if the part document yields no solid bodies or if
        /// every face fails to tessellate -- caller falls back to FE mesh.
        /// </summary>
        private CadDisplayMesh ExtractCadDisplayTessellation(IModelDoc2 modelDoc)
        {
            if (modelDoc == null) return null;
            IPartDoc partDoc = modelDoc as IPartDoc;
            if (partDoc == null)
            {
                Console.WriteLine("    FEA Stage 3: CAD path skipped -- document is not a part.");
                return null;
            }

            // Primary: ITessellation with explicit chord/angular tolerance.
            // GetTessTriangles only honours the document's image-quality
            // slider (swImageQualityShadedDeviation), and on this build of
            // SolidWorks the cached display tessellation does not invalidate
            // when the slider is bumped at runtime — verified empirically:
            // setting chord deviation 7.10e-5 -> 1.00e-5 + GraphicsRedraw2
            // still yielded the same 276-vertex / 568-triangle mesh. So we
            // ask for a fresh tessellation directly via the modern API.
            //
            // 20 micron chord deviation + 2 degree angular deviation gives
            // a smooth hole boundary and curved-edge silhouette while still
            // collapsing co-planar regions of the plate to a moderate number
            // of large triangles. At 50um/5deg the Mounting Plate produced
            // 738 verts which still looked faceted; tightening to 20um/2deg
            // lands in the low-thousands target without over-tessellating.
            const double CHORD_TOL_M = 2.0e-5;       // 20 microns
            const double ANGLE_TOL_RAD = 0.0349066;  // 2 degrees
            CadDisplayMesh dense = ExtractCadDisplayMeshViaITessellation(
                partDoc, CHORD_TOL_M, ANGLE_TOL_RAD);
            if (dense != null && dense.VertexCount > 0)
            {
                return dense;
            }

            // Fallback: legacy IFace2.GetTessTriangles walk. Only reached
            // when ITessellation is unavailable / throws / produces no
            // vertices. The image-quality bump below tightens the slider so
            // the legacy walk gets denser tessellation than the document's
            // saved setting would otherwise produce; the change is restored
            // in finally to avoid mutating the user's saved quality.
            Console.WriteLine("    FEA Stage 3: ITessellation unavailable or empty; falling back to IFace2.GetTessTriangles legacy walk");

            double originalShadedDeviation = -1.0;
            bool qualityBumped = false;
            const double TARGET_DEVIATION = 1.0e-5;
            try
            {
                var ext = modelDoc.Extension;
                if (ext != null)
                {
                    originalShadedDeviation = ext.GetUserPreferenceDouble(
                        (int)swUserPreferenceDoubleValue_e.swImageQualityShadedDeviation,
                        (int)swUserPreferenceOption_e.swDetailingNoOptionSpecified);
                    if (originalShadedDeviation > TARGET_DEVIATION)
                    {
                        ext.SetUserPreferenceDouble(
                            (int)swUserPreferenceDoubleValue_e.swImageQualityShadedDeviation,
                            (int)swUserPreferenceOption_e.swDetailingNoOptionSpecified,
                            TARGET_DEVIATION);
                        modelDoc.GraphicsRedraw2();
                        qualityBumped = true;
                    }
                }
            }
            catch (Exception ex)
            {
                Console.WriteLine($"    FEA Stage 3: image-quality bump failed ({ex.Message}); using current document tessellation");
            }

            try
            {
                return ExtractCadDisplayTessellationCore(partDoc);
            }
            finally
            {
                if (qualityBumped)
                {
                    try
                    {
                        modelDoc.Extension.SetUserPreferenceDouble(
                            (int)swUserPreferenceDoubleValue_e.swImageQualityShadedDeviation,
                            (int)swUserPreferenceOption_e.swDetailingNoOptionSpecified,
                            originalShadedDeviation);
                    }
                    catch { /* best-effort restore */ }
                }
            }
        }

        /// <summary>
        /// Build a CAD display mesh using the modern ITessellation API.
        ///
        /// Unlike IFace2.GetTessTriangles — which honours only the document's
        /// saved image-quality slider and may return a stale cached mesh —
        /// ITessellation generates a fresh tessellation against an explicit
        /// surface chord tolerance and angular tolerance. That gives the
        /// extractor deterministic control over visual quality regardless
        /// of the part's saved state.
        ///
        /// The result is per-body vertex/facet lists; vertex IDs are local
        /// to each body, so we dedupe positions across bodies via the same
        /// 1 nm position-key dictionary used by the legacy path.
        ///
        /// Returns null when the part has no solid bodies, or when every
        /// body fails to tessellate. Callers fall back to the legacy walk.
        /// </summary>
        private CadDisplayMesh ExtractCadDisplayMeshViaITessellation(
            IPartDoc partDoc, double chordTolM, double angleTolRad)
        {
            if (partDoc == null) return null;

            object bodiesObj;
            try { bodiesObj = partDoc.GetBodies2((int)swBodyType_e.swSolidBody, true); }
            catch (Exception ex) { Console.WriteLine($"    FEA Stage 3: ITessellation GetBodies2 threw: {ex.Message}"); return null; }
            if (bodiesObj == null) return null;
            object[] bodies = bodiesObj as object[];
            if (bodies == null || bodies.Length == 0) return null;

            var dedup = new Dictionary<(long qx, long qy, long qz), int>();
            var posList = new List<float>();
            var normList = new List<float>();
            var idxList = new List<uint>();
            int bodiesOk = 0, bodiesFailed = 0, totalFacets = 0;

            foreach (object bodyObj in bodies)
            {
                IBody2 body = bodyObj as IBody2;
                if (body == null) continue;

                ITessellation tess;
                try
                {
                    object tessObj = body.GetTessellation(null);
                    tess = tessObj as ITessellation;
                }
                catch (Exception ex)
                {
                    Console.WriteLine($"    FEA Stage 3: ITessellation: GetTessellation threw {ex.Message}");
                    bodiesFailed++;
                    continue;
                }
                if (tess == null) { bodiesFailed++; continue; }

                try
                {
                    tess.NeedFaceFacetMap = false;
                    tess.NeedVertexNormal = true;
                    tess.NeedVertexParams = false;
                    tess.NeedEdgeFinMap = false;
                    tess.NeedErrorList = false;
                    tess.ImprovedQuality = true;
                    // swTesselationMatchType_e.swTesselationMatchFacetTopology = 1
                    tess.MatchType = 1;
                    tess.SurfacePlaneTolerance = chordTolM;
                    tess.SurfacePlaneAngleTolerance = angleTolRad;
                    tess.CurveChordTolerance = chordTolM;
                    tess.CurveChordAngleTolerance = angleTolRad;
                }
                catch (Exception ex)
                {
                    Console.WriteLine($"    FEA Stage 3: ITessellation property setup threw {ex.Message}");
                    bodiesFailed++;
                    continue;
                }

                bool ok;
                try { ok = tess.Tessellate(); }
                catch (Exception ex)
                {
                    Console.WriteLine($"    FEA Stage 3: ITessellation Tessellate() threw {ex.Message}");
                    bodiesFailed++;
                    continue;
                }
                if (!ok) { bodiesFailed++; continue; }

                int vCount = tess.GetVertexCount();
                int fCount = tess.GetFacetCount();
                if (vCount == 0 || fCount == 0) { bodiesFailed++; continue; }

                int[] bodyVertToGlobal = new int[vCount];
                for (int v = 0; v < vCount; v++)
                {
                    object pObj = tess.GetVertexPoint(v);
                    double[] p = pObj as double[];
                    if (p == null || p.Length < 3) { bodyVertToGlobal[v] = -1; continue; }
                    float px = (float)p[0], py = (float)p[1], pz = (float)p[2];

                    float nx = 0, ny = 0, nz = 1;
                    try
                    {
                        object nObj = tess.GetVertexNormal(v);
                        double[] n = nObj as double[];
                        if (n != null && n.Length >= 3)
                        {
                            nx = (float)n[0]; ny = (float)n[1]; nz = (float)n[2];
                        }
                    }
                    catch { /* synthesise per-face normal below if normal missing */ }

                    var key = (
                        (long)Math.Round(px * 1e9),
                        (long)Math.Round(py * 1e9),
                        (long)Math.Round(pz * 1e9));
                    int existing;
                    if (dedup.TryGetValue(key, out existing))
                    {
                        bodyVertToGlobal[v] = existing;
                    }
                    else
                    {
                        int newIdx = posList.Count / 3;
                        dedup[key] = newIdx;
                        posList.Add(px); posList.Add(py); posList.Add(pz);
                        normList.Add(nx); normList.Add(ny); normList.Add(nz);
                        bodyVertToGlobal[v] = newIdx;
                    }
                }

                for (int f = 0; f < fCount; f++)
                {
                    int[] fins;
                    try { fins = ConvertToIntArray(tess.GetFacetFins(f)); }
                    catch { continue; }
                    if (fins == null || fins.Length < 3) continue;

                    int v0, v1, v2;
                    try
                    {
                        // Each facet has 3 fins forming a closed loop:
                        //   fin0.start -> fin0.end == fin1.start -> ...
                        // The triangle's 3 vertices are the start of each fin.
                        int[] fv0 = ConvertToIntArray(tess.GetFinVertices(fins[0]));
                        int[] fv1 = ConvertToIntArray(tess.GetFinVertices(fins[1]));
                        int[] fv2 = ConvertToIntArray(tess.GetFinVertices(fins[2]));
                        if (fv0 == null || fv1 == null || fv2 == null ||
                            fv0.Length < 2 || fv1.Length < 2 || fv2.Length < 2) continue;
                        v0 = fv0[0]; v1 = fv1[0]; v2 = fv2[0];
                    }
                    catch { continue; }

                    if (v0 < 0 || v0 >= vCount || v1 < 0 || v1 >= vCount || v2 < 0 || v2 >= vCount) continue;
                    int g0 = bodyVertToGlobal[v0];
                    int g1 = bodyVertToGlobal[v1];
                    int g2 = bodyVertToGlobal[v2];
                    if (g0 < 0 || g1 < 0 || g2 < 0) continue;
                    if (g0 == g1 || g1 == g2 || g0 == g2) continue; // skip degenerate
                    idxList.Add((uint)g0);
                    idxList.Add((uint)g1);
                    idxList.Add((uint)g2);
                }

                totalFacets += fCount;
                bodiesOk++;
            }

            if (posList.Count == 0 || idxList.Count == 0)
            {
                Console.WriteLine($"    FEA Stage 3: ITessellation produced no usable mesh ({bodiesOk} bodies ok, {bodiesFailed} failed)");
                return null;
            }

            Console.WriteLine($"    FEA Stage 3: ITessellation -> {posList.Count / 3:N0} unique vertices, {idxList.Count / 3:N0} triangles ({bodiesOk} bodies ok, {bodiesFailed} failed; chord {chordTolM * 1000.0:F3}mm, angle {angleTolRad * 180.0 / Math.PI:F1}deg)");

            return new CadDisplayMesh
            {
                Positions = posList.ToArray(),
                Normals = normList.ToArray(),
                Indices = idxList.ToArray(),
                VertexCount = posList.Count / 3,
                TriangleCount = idxList.Count / 3,
            };
        }

        /// <summary>
        /// Refine a triangle mesh by midpoint-subdividing every triangle with
        /// an edge longer than <paramref name="maxEdgeLengthM"/>. The result
        /// is watertight: midpoint vertices are deduplicated across shared
        /// edges, so neighbour triangles sharing a split edge always reference
        /// the same midpoint vertex (no T-junctions / cracks).
        ///
        /// Subdivision pattern is the standard "red-green" 1-to-{2,3,4} split,
        /// driven by how many of a triangle's 3 edges exceed the threshold:
        ///
        ///   0 long edges -> keep as-is              (1 triangle)
        ///   1 long edge  -> bisect along that edge  (2 triangles)
        ///   2 long edges -> 3 sub-triangles
        ///   3 long edges -> 4 sub-triangles (1-to-4 red split)
        ///
        /// Iterates up to <paramref name="maxPasses"/> times. One pass halves
        /// the longest edge, so 6 passes can reduce an oversize edge by 64×;
        /// in practice 1-2 passes are enough for a part-scale mesh.
        ///
        /// Boundary shape is preserved: a midpoint of a boundary edge is
        /// itself on the boundary (linear interpolation between two boundary
        /// vertices). Curved boundaries that already got dense ITessellation
        /// (hole, fillet) come in with edges short enough to skip refinement,
        /// while the long edges spanning flat plate regions get split.
        ///
        /// Vertex normals at midpoints are linearly interpolated and
        /// renormalised — fine for downstream colour shading because the
        /// refined regions are exactly the flat zones where neighbour
        /// triangles already share a normal.
        /// </summary>
        private CadDisplayMesh RefineCadDisplayMesh(
            CadDisplayMesh src,
            double maxEdgeLengthM,
            int maxPasses,
            out int passesUsed,
            out int totalTriangleSplits)
        {
            passesUsed = 0;
            totalTriangleSplits = 0;
            if (src == null || src.VertexCount == 0 || src.TriangleCount == 0) return src;
            if (maxEdgeLengthM <= 0 || maxPasses <= 0) return src;

            var positions = new List<float>(src.Positions);
            var normals = new List<float>(src.Normals);
            var indices = new List<uint>(src.Indices);
            double thresholdSq = maxEdgeLengthM * maxEdgeLengthM;

            for (int pass = 0; pass < maxPasses; pass++)
            {
                int triCount = indices.Count / 3;
                var edgeMid = new Dictionary<(uint lo, uint hi), uint>();

                // Pass 1: find every edge that exceeds the threshold and
                // create its midpoint vertex (deduped across triangles that
                // share the edge).
                for (int t = 0; t < triCount; t++)
                {
                    uint a = indices[t * 3];
                    uint b = indices[t * 3 + 1];
                    uint c = indices[t * 3 + 2];
                    MarkLongEdge(positions, normals, edgeMid, a, b, thresholdSq);
                    MarkLongEdge(positions, normals, edgeMid, b, c, thresholdSq);
                    MarkLongEdge(positions, normals, edgeMid, c, a, thresholdSq);
                }

                if (edgeMid.Count == 0) break; // no long edges left

                passesUsed = pass + 1;

                // Pass 2: emit subdivided triangles. A triangle is split based
                // on which of its 3 edges have midpoints in edgeMid.
                var newIndices = new List<uint>(indices.Count * 4);
                int passSplits = 0;
                for (int t = 0; t < triCount; t++)
                {
                    uint a = indices[t * 3];
                    uint b = indices[t * 3 + 1];
                    uint c = indices[t * 3 + 2];

                    bool hasAB = TryGetMid(edgeMid, a, b, out uint mAB);
                    bool hasBC = TryGetMid(edgeMid, b, c, out uint mBC);
                    bool hasCA = TryGetMid(edgeMid, c, a, out uint mCA);

                    int splitMask = (hasAB ? 1 : 0) | (hasBC ? 2 : 0) | (hasCA ? 4 : 0);

                    switch (splitMask)
                    {
                        case 0:
                            newIndices.Add(a); newIndices.Add(b); newIndices.Add(c);
                            break;
                        case 1: // AB split
                            newIndices.Add(a); newIndices.Add(mAB); newIndices.Add(c);
                            newIndices.Add(mAB); newIndices.Add(b); newIndices.Add(c);
                            passSplits++;
                            break;
                        case 2: // BC split
                            newIndices.Add(a); newIndices.Add(b); newIndices.Add(mBC);
                            newIndices.Add(a); newIndices.Add(mBC); newIndices.Add(c);
                            passSplits++;
                            break;
                        case 4: // CA split
                            newIndices.Add(a); newIndices.Add(b); newIndices.Add(mCA);
                            newIndices.Add(mCA); newIndices.Add(b); newIndices.Add(c);
                            passSplits++;
                            break;
                        case 3: // AB + BC
                            newIndices.Add(a); newIndices.Add(mAB); newIndices.Add(mBC);
                            newIndices.Add(mAB); newIndices.Add(b); newIndices.Add(mBC);
                            newIndices.Add(a); newIndices.Add(mBC); newIndices.Add(c);
                            passSplits++;
                            break;
                        case 5: // AB + CA
                            newIndices.Add(a); newIndices.Add(mAB); newIndices.Add(mCA);
                            newIndices.Add(mAB); newIndices.Add(b); newIndices.Add(c);
                            newIndices.Add(mAB); newIndices.Add(c); newIndices.Add(mCA);
                            passSplits++;
                            break;
                        case 6: // BC + CA
                            newIndices.Add(a); newIndices.Add(b); newIndices.Add(mCA);
                            newIndices.Add(mCA); newIndices.Add(b); newIndices.Add(mBC);
                            newIndices.Add(mCA); newIndices.Add(mBC); newIndices.Add(c);
                            passSplits++;
                            break;
                        case 7: // all 3 (red 1-to-4 split)
                            newIndices.Add(a); newIndices.Add(mAB); newIndices.Add(mCA);
                            newIndices.Add(mAB); newIndices.Add(b); newIndices.Add(mBC);
                            newIndices.Add(mCA); newIndices.Add(mBC); newIndices.Add(c);
                            newIndices.Add(mAB); newIndices.Add(mBC); newIndices.Add(mCA);
                            passSplits++;
                            break;
                    }
                }

                indices = newIndices;
                totalTriangleSplits += passSplits;
            }

            return new CadDisplayMesh
            {
                Positions = positions.ToArray(),
                Normals = normals.ToArray(),
                Indices = indices.ToArray(),
                VertexCount = positions.Count / 3,
                TriangleCount = indices.Count / 3,
            };
        }

        private static void MarkLongEdge(
            List<float> positions, List<float> normals,
            Dictionary<(uint lo, uint hi), uint> edgeMid,
            uint v0, uint v1, double thresholdSq)
        {
            (uint lo, uint hi) key = v0 < v1 ? (v0, v1) : (v1, v0);
            if (edgeMid.ContainsKey(key)) return;

            int i0 = (int)v0, i1 = (int)v1;
            float p0x = positions[i0 * 3], p0y = positions[i0 * 3 + 1], p0z = positions[i0 * 3 + 2];
            float p1x = positions[i1 * 3], p1y = positions[i1 * 3 + 1], p1z = positions[i1 * 3 + 2];
            float dx = p0x - p1x, dy = p0y - p1y, dz = p0z - p1z;
            double lenSq = (double)dx * dx + (double)dy * dy + (double)dz * dz;
            if (lenSq <= thresholdSq) return;

            float mx = (p0x + p1x) * 0.5f;
            float my = (p0y + p1y) * 0.5f;
            float mz = (p0z + p1z) * 0.5f;

            float n0x = normals[i0 * 3], n0y = normals[i0 * 3 + 1], n0z = normals[i0 * 3 + 2];
            float n1x = normals[i1 * 3], n1y = normals[i1 * 3 + 1], n1z = normals[i1 * 3 + 2];
            float nx = n0x + n1x, ny = n0y + n1y, nz = n0z + n1z;
            double mag = Math.Sqrt((double)nx * nx + (double)ny * ny + (double)nz * nz);
            if (mag > 1e-12) { nx = (float)(nx / mag); ny = (float)(ny / mag); nz = (float)(nz / mag); }
            else { nx = n0x; ny = n0y; nz = n0z; }

            uint newIdx = (uint)(positions.Count / 3);
            positions.Add(mx); positions.Add(my); positions.Add(mz);
            normals.Add(nx); normals.Add(ny); normals.Add(nz);
            edgeMid[key] = newIdx;
        }

        private static bool TryGetMid(
            Dictionary<(uint lo, uint hi), uint> edgeMid,
            uint v0, uint v1, out uint mid)
        {
            (uint lo, uint hi) key = v0 < v1 ? (v0, v1) : (v1, v0);
            return edgeMid.TryGetValue(key, out mid);
        }

        private CadDisplayMesh ExtractCadDisplayTessellationCore(IPartDoc partDoc)
        {
            object bodiesObj;
            try { bodiesObj = partDoc.GetBodies2((int)swBodyType_e.swSolidBody, true); }
            catch (Exception ex) { Console.WriteLine($"    FEA Stage 3: GetBodies2 threw: {ex.Message}"); return null; }
            if (bodiesObj == null) return null;
            object[] bodies = bodiesObj as object[];
            if (bodies == null || bodies.Length == 0) return null;

            // Position-keyed dedupe: tessellation returns shared edges as
            // duplicate vertex entries on every adjacent face. Round to 1 nm
            // (1e-9 m) and key by the full quantised (qx, qy, qz) tuple.
            // ValueTuple's auto-generated GetHashCode/Equals operate on the
            // full 64-bit components -- collision-free across the entire
            // coordinate range a SolidWorks part will produce. The previous
            // bit-packed-into-long key truncated each axis to 21 bits, so
            // points separated by multiples of 2.097 mm aliased and merged.
            var dedup = new Dictionary<(long qx, long qy, long qz), int>();
            var posList = new List<float>();
            var normList = new List<float>();
            var idxList = new List<uint>();

            int facesProcessed = 0, facesFailed = 0;

            foreach (object bodyObj in bodies)
            {
                IBody2 body = bodyObj as IBody2;
                if (body == null) continue;

                IFace2 face = body.GetFirstFace() as IFace2;
                while (face != null)
                {
                    float[] tv = null, tn = null;
                    try
                    {
                        tv = ConvertToFloatArray(face.GetTessTriangles(true)); // true = no unit conversion (meters)
                        tn = ConvertToFloatArray(face.GetTessNorms());
                    }
                    catch { facesFailed++; face = face.GetNextFace() as IFace2; continue; }

                    if (tv == null || tv.Length < 9 || tv.Length % 9 != 0)
                    {
                        facesFailed++;
                        face = face.GetNextFace() as IFace2;
                        continue;
                    }
                    if (tn == null || tn.Length != tv.Length)
                    {
                        // Synthesize per-vertex normals from the face plane
                        tn = new float[tv.Length];
                        for (int i = 0; i + 8 < tv.Length; i += 9)
                        {
                            double ax = tv[i + 3] - tv[i + 0], ay = tv[i + 4] - tv[i + 1], az = tv[i + 5] - tv[i + 2];
                            double bx = tv[i + 6] - tv[i + 0], by = tv[i + 7] - tv[i + 1], bz = tv[i + 8] - tv[i + 2];
                            double nx = ay * bz - az * by, ny = az * bx - ax * bz, nz = ax * by - ay * bx;
                            double mag = Math.Sqrt(nx * nx + ny * ny + nz * nz);
                            if (mag > 1e-12) { nx /= mag; ny /= mag; nz /= mag; }
                            for (int k = 0; k < 3; k++)
                            {
                                tn[i + k * 3 + 0] = (float)nx;
                                tn[i + k * 3 + 1] = (float)ny;
                                tn[i + k * 3 + 2] = (float)nz;
                            }
                        }
                    }

                    int triCount = tv.Length / 9;
                    for (int t = 0; t < triCount; t++)
                    {
                        for (int v = 0; v < 3; v++)
                        {
                            int srcIdx = t * 9 + v * 3;
                            float px = tv[srcIdx + 0], py = tv[srcIdx + 1], pz = tv[srcIdx + 2];
                            // 1 nm dedupe quantisation
                            var key = (
                                (long)Math.Round(px * 1e9),
                                (long)Math.Round(py * 1e9),
                                (long)Math.Round(pz * 1e9));
                            int reuseIdx;
                            if (dedup.TryGetValue(key, out reuseIdx))
                            {
                                idxList.Add((uint)reuseIdx);
                            }
                            else
                            {
                                int newIdx = posList.Count / 3;
                                dedup[key] = newIdx;
                                posList.Add(px); posList.Add(py); posList.Add(pz);
                                normList.Add(tn[srcIdx + 0]); normList.Add(tn[srcIdx + 1]); normList.Add(tn[srcIdx + 2]);
                                idxList.Add((uint)newIdx);
                            }
                        }
                    }
                    facesProcessed++;
                    face = face.GetNextFace() as IFace2;
                }
            }

            if (posList.Count == 0)
            {
                Console.WriteLine($"    FEA Stage 3: CAD tessellation produced no vertices ({facesProcessed} faces processed, {facesFailed} failed)");
                return null;
            }

            Console.WriteLine($"    FEA Stage 3: CAD tessellation -> {posList.Count / 3:N0} unique vertices, {idxList.Count / 3:N0} triangles ({facesProcessed} faces ok, {facesFailed} failed)");

            return new CadDisplayMesh
            {
                Positions = posList.ToArray(),
                Normals = normList.ToArray(),
                Indices = idxList.ToArray(),
                VertexCount = posList.Count / 3,
                TriangleCount = idxList.Count / 3,
            };
        }

        /// <summary>
        /// Uniform spatial-hash index over FE surface nodes. Build is O(N);
        /// nearest-K query is O(K * cellOccupants) with cell size tuned to
        /// average node spacing. For typical FE meshes this is much faster
        /// than a brute-force O(N) scan per CAD vertex and avoids the kd-tree
        /// construction overhead.
        /// </summary>
        private class SurfaceNodeSpatialIndex
        {
            private readonly float[] _x, _y, _z; // surface-vertex-ordered
            private readonly int _count;
            private readonly double _cell;
            private readonly Dictionary<long, List<int>> _grid = new Dictionary<long, List<int>>();

            public double MeanSpacing { get; }

            public SurfaceNodeSpatialIndex(float[] surfPositions)
            {
                _count = surfPositions.Length / 3;
                _x = new float[_count]; _y = new float[_count]; _z = new float[_count];
                for (int i = 0; i < _count; i++)
                {
                    _x[i] = surfPositions[i * 3];
                    _y[i] = surfPositions[i * 3 + 1];
                    _z[i] = surfPositions[i * 3 + 2];
                }

                // Estimate mean spacing: bbox volume^(1/3) / count^(1/3).
                if (_count > 1)
                {
                    float xn = float.MaxValue, xp = float.MinValue;
                    float yn = float.MaxValue, yp = float.MinValue;
                    float zn = float.MaxValue, zp = float.MinValue;
                    for (int i = 0; i < _count; i++)
                    {
                        if (_x[i] < xn) xn = _x[i]; if (_x[i] > xp) xp = _x[i];
                        if (_y[i] < yn) yn = _y[i]; if (_y[i] > yp) yp = _y[i];
                        if (_z[i] < zn) zn = _z[i]; if (_z[i] > zp) zp = _z[i];
                    }
                    double dx = xp - xn, dy = yp - yn, dz = zp - zn;
                    // For thin parts (e.g. plates), the smallest dim is dominated by thickness;
                    // use the geometric mean of the two largest dims as a more representative scale.
                    double[] dims = { dx, dy, dz };
                    Array.Sort(dims);
                    double area = dims[1] * dims[2];
                    MeanSpacing = Math.Sqrt(area / Math.Max(1, _count));
                    _cell = Math.Max(MeanSpacing * 2.0, 1e-6);
                }
                else
                {
                    MeanSpacing = 1e-3;
                    _cell = 1e-3;
                }

                // Bucket into grid
                for (int i = 0; i < _count; i++)
                {
                    long key = HashCell(_x[i], _y[i], _z[i]);
                    List<int> bucket;
                    if (!_grid.TryGetValue(key, out bucket))
                    {
                        bucket = new List<int>();
                        _grid[key] = bucket;
                    }
                    bucket.Add(i);
                }
            }

            private long HashCell(double x, double y, double z)
            {
                long ix = (long)Math.Floor(x / _cell);
                long iy = (long)Math.Floor(y / _cell);
                long iz = (long)Math.Floor(z / _cell);
                // Pack to a single long; collisions across cells are acceptable
                // for the bucket-coalescing dictionary because we still verify
                // by actual distance in the search loop.
                unchecked
                {
                    long h = ix * 73856093L;
                    h = (h ^ iy) * 19349663L;
                    h = (h ^ iz) * 83492791L;
                    return h;
                }
            }

            /// <summary>
            /// Find the K nearest surface-vertex indices to (x,y,z), expanding
            /// the cell search radius outward until at least K candidates are
            /// found. Returns sorted by distance ascending. Each entry: (index, distance).
            /// </summary>
            public (int idx, double dist)[] FindNearestK(double x, double y, double z, int k)
            {
                long ix0 = (long)Math.Floor(x / _cell);
                long iy0 = (long)Math.Floor(y / _cell);
                long iz0 = (long)Math.Floor(z / _cell);
                var found = new List<(int idx, double dist)>(k * 2);
                for (int radius = 0; radius < 8; radius++)
                {
                    for (long dx = -radius; dx <= radius; dx++)
                        for (long dy = -radius; dy <= radius; dy++)
                            for (long dz = -radius; dz <= radius; dz++)
                            {
                                // Only visit shell of this radius (interior was visited at smaller radius)
                                if (radius > 0
                                    && Math.Abs(dx) < radius && Math.Abs(dy) < radius && Math.Abs(dz) < radius)
                                    continue;
                                long ix = ix0 + dx, iy = iy0 + dy, iz = iz0 + dz;
                                long h;
                                unchecked
                                {
                                    h = ix * 73856093L;
                                    h = (h ^ iy) * 19349663L;
                                    h = (h ^ iz) * 83492791L;
                                }
                                List<int> bucket;
                                if (!_grid.TryGetValue(h, out bucket)) continue;
                                foreach (int i in bucket)
                                {
                                    double ddx = _x[i] - x, ddy = _y[i] - y, ddz = _z[i] - z;
                                    found.Add((i, Math.Sqrt(ddx * ddx + ddy * ddy + ddz * ddz)));
                                }
                            }
                    if (found.Count >= k) break;
                }
                found.Sort((a, b) => a.dist.CompareTo(b.dist));
                if (found.Count > k) found = found.GetRange(0, k);
                return found.ToArray();
            }
        }

        /// <summary>
        /// Build the CAD-projected FeaMeshData. Returns null if any
        /// prerequisite is missing or the projection cannot produce a coherent
        /// surface. Caller falls back to the FE-mesh path.
        /// </summary>
        private FeaMeshData TryBuildCadProjectedFeaMesh(
            IModelDoc2 modelDoc,
            float[] feSurfacePositions,        // surfVertCount * 3, meters
            double[] feSurfaceStress,          // Pa, per-FE-surface-vertex
            double[] feSurfaceDispX,           // meters
            double[] feSurfaceDispY,
            double[] feSurfaceDispZ,
            bool feSurfaceHasDisplacement,
            bool[] feStressCovered,            // per-FE-surface-node: stressValues was actually populated
            bool[] feDispCovered,              // per-FE-surface-node: disp arrays were actually populated
            int[] feTriIndices,                // 3*Nt surface-vertex indices for the sharp/peak path; null disables peak preservation
            float[] feTriCentroids,            // 3*Nt centroids in m (parallel with feTriIndices)
            double[] feTriVon,                 // Nt parent-element raw VON in Pa (parallel with feTriIndices)
            FeaResultsSummary summary)
        {
            int feCount = feSurfacePositions.Length / 3;
            if (feCount < 4)
            {
                Console.WriteLine("    FEA Stage 3: CAD path skipped -- too few FE surface nodes for projection.");
                return null;
            }

            // Coverage gate: if too few FE surface nodes carry real solver
            // stress, IDW projection would weight default-zero defaults into
            // the average and produce an artificial "blue" surface where
            // there's no data. Refuse the CAD path and fall back to the FE
            // mesh, which renders honest gaps as zero-stress rather than
            // smoothing them into a misleading gradient.
            int feStressCoveredCount = 0;
            if (feStressCovered != null)
            {
                for (int i = 0; i < feStressCovered.Length; i++)
                    if (feStressCovered[i]) feStressCoveredCount++;
            }
            const double MIN_STRESS_COVERAGE = 0.95;
            double feStressCoveragePct = feCount > 0 ? (double)feStressCoveredCount / feCount : 0;
            if (feStressCoveragePct < MIN_STRESS_COVERAGE)
            {
                Console.WriteLine($"    FEA Stage 3: CAD path skipped -- FE stress coverage {feStressCoveragePct:P1} < required {MIN_STRESS_COVERAGE:P0}. Fallback to FE-mesh path so the missing region is rendered honestly.");
                Warn(summary, $"CAD-tessellation path declined: FE stress coverage {feStressCoveragePct:P1} below {MIN_STRESS_COVERAGE:P0} threshold. CAD projection requires high coverage so unsolved nodes don't bias the IDW average toward synthetic zero stress.");
                return null;
            }

            CadDisplayMesh cadBase = ExtractCadDisplayTessellation(modelDoc);
            if (cadBase == null) return null;

            var index = new SurfaceNodeSpatialIndex(feSurfacePositions);
            const int K = 4;

            // Refine the CAD display mesh so flat plate faces don't render as
            // giant triangular wedges with starburst stress contours. The
            // ITessellation upstream is curvature-driven: it only refines
            // where geometry curves, so a flat plate keeps a handful of huge
            // triangles even at tight chord tolerance. After it returns, run
            // a watertight midpoint-subdivision pass that splits any triangle
            // with an edge longer than ~2.5× the mean FE node spacing — i.e.
            // any edge across which the IDW interpolation would smear a
            // visibly wide stress band.
            //
            // Refinement is decoupled from the FE coverage gates above and
            // adds zero new uncovered regions: every refined vertex is a
            // midpoint of an existing CAD edge, so it lands strictly within
            // the original CAD surface footprint that already met the 95%
            // coverage threshold. visualization.mode and approximate flags
            // do not change.
            // Threshold = 3.5 × mean FE spacing. The user's target band is
            // 2-3× FE spacing per edge, but midpoint subdivision compounds
            // unevenly: at 2.5× the algorithm needed 5 passes to satisfy the
            // bound and produced 43k vertices on the Mounting Plate — way
            // beyond the "several thousand verts" target. 3.5× lands in the
            // single-digit thousands while still cutting the largest plate
            // wedges into many sub-triangles.
            //
            // maxPasses is capped at 3: each pass halves the longest edge,
            // so 3 passes can shrink an oversize edge by 8×. Beyond that the
            // returns diminish quickly and the GLB grows past what the
            // viewer needs.
            double refinementEdgeThresholdM = Math.Max(index.MeanSpacing * 3.5, 1e-5);
            int refinementPasses = 0;
            int refinementSplits = 0;
            CadDisplayMesh cad = RefineCadDisplayMesh(
                cadBase,
                refinementEdgeThresholdM,
                maxPasses: 3,
                out refinementPasses,
                out refinementSplits);
            string refinementMethod = (refinementSplits > 0)
                ? "midpoint-subdivision-edge-threshold"
                : "none";
            double thresholdRatio = index.MeanSpacing > 0 ? refinementEdgeThresholdM / index.MeanSpacing : 0.0;
            Console.WriteLine($"    FEA Stage 3: display mesh refinement ({refinementMethod}): base {cadBase.VertexCount:N0} verts / {cadBase.TriangleCount:N0} tris -> refined {cad.VertexCount:N0} verts / {cad.TriangleCount:N0} tris (edge threshold {refinementEdgeThresholdM * 1000.0:F3} mm = {thresholdRatio:F2}× mean FE spacing {index.MeanSpacing * 1000.0:F3} mm; {refinementPasses} passes, {refinementSplits:N0} triangle splits)");

            double[] cadStress = new double[cad.VertexCount];
            double[] cadDispX = new double[cad.VertexCount];
            double[] cadDispY = new double[cad.VertexCount];
            double[] cadDispZ = new double[cad.VertexCount];
            double maxStress = 0;
            int peakIdx = 0;

            double maxNeighbourDist = 0;
            int farVerts = 0;
            int cadVertsNoStressNeighbour = 0;
            int cadVertsNoDispNeighbour = 0;
            double farThreshold = Math.Max(index.MeanSpacing * 2.0, 1e-6);

            // Stress projection: K nearest FE surface nodes weighted by a
            // narrow Gaussian kernel (sigma tied to mean FE node spacing).
            //
            // History of approaches that didn't fit the user's needs:
            //   * K=1 nearest-element direct  -> piecewise-constant Voronoi
            //                                    cells, visible "wedge fans"
            //   * K=8 closest-point-on-triangle barycentric
            //                                 -> piecewise-linear, removed
            //                                    Voronoi cells but exposed
            //                                    the FE-triangle structure
            //                                    as G1-discontinuous "shard"
            //                                    texture along triangle edges
            //   * K=4 IDW over FE-node values (1/d^2) was the original path
            //                                 -> too diffusive, "green wash"
            //
            // A narrow Gaussian kernel over the FE surface NODES is C-infinity
            // smooth (no creases at node/triangle boundaries), local enough
            // to preserve the hotspot at the hole, and avoids the diffusion
            // of a wide IDW. With sigma = 0.7 * mean FE spacing:
            //   - neighbours at 1*spacing get weight  exp(-1/(2*0.49)) ≈ 0.36
            //   - neighbours at 2*spacing get weight  exp(-4/(2*0.49)) ≈ 0.017
            //   - neighbours at 3*spacing get weight  exp(-9/(2*0.49)) ≈ 0.0001
            // so 2-4 nearest nodes carry essentially all the weight, with
            // distant low-stress nodes contributing negligibly even at the
            // peak. K=12 candidate slots are ample to ensure all in-radius
            // nodes are picked up regardless of cluster geometry.
            //
            // Displacement projection stays on the existing K=4 1/d^2 path:
            // displacement is genuinely per-node from the solver, the morph
            // shape already reads correctly, and we don't want to risk
            // regressing it.
            const int STRESS_K = 12;
            double sigma = 0.7 * Math.Max(index.MeanSpacing, 1e-6);
            double inv2SigmaSq = 1.0 / (2.0 * sigma * sigma);
            double smoothingRadiusM = 3.0 * sigma;

            // ---- Peak-preservation: edge-aware blend of smooth + sharp ----
            // The Gaussian-only smooth field is right for global continuity
            // but it averages the local maximum at the stress concentration
            // downward, so the visible contour loses the red/orange hotspot
            // SolidWorks shows near the hole. To recover it without bringing
            // back the FE-triangle "shard" texture across flat regions, we
            // compute two display fields and blend per CAD vertex:
            //
            //   smooth = Gaussian-weighted K=12 nearest FE surface nodes
            //            (sigma = 0.7 * meanFESpacing, 3-sigma cutoff)
            //   sharp  = closest-point-on-triangle barycentric over the FE
            //            surface triangle field (preserves nodal peaks
            //            exactly when the CAD vertex sits at a node)
            //
            // The blend factor alpha is driven by *local stress spread* — the
            // (max - min) of stress values across the K nearest covered FE
            // nodes, normalised by the colour-mapping range:
            //
            //   spread_norm = (s_max - s_min) / (colorMaxPa - colorMinPa)
            //
            // smoothstep(blend_min, blend_max, spread_norm) gives alpha:
            //   - low spread (locally uniform stress) -> alpha ~ 0
            //                                            -> smooth dominates
            //   - high spread (near a concentration)  -> alpha ~ 1
            //                                            -> sharp dominates
            //
            // The thresholds are explicit, recorded in the manifest, and
            // tied to physical (not visual) variance.
            // Blend thresholds for the smooth/sharp mix, expressed as a
            // fraction of the colour range. Basis is delta = sharp - smooth
            // (where sharp = nearest-FE-triangle's parent element raw VON,
            // smooth = Gaussian-weighted nodal-averaged stress). Positive
            // delta means the per-element peak is being hidden by nodal
            // smoothing; that's exactly where we want to expose the sharp
            // value:
            //   - delta < 5% of colour range:  alpha = 0 (smooth dominates;
            //     sharp would add nothing the smooth field doesn't already
            //     show, so no Voronoi-cell texture leaks anywhere).
            //   - delta > 20% of colour range: alpha = 1 (sharp dominates;
            //     a per-element peak is significantly hidden — let it
            //     through).
            //   - smoothstep ramp in between.
            //
            // Earlier "local-spread" basis underestimated alpha at the
            // hotspot because nodal smoothing flattened the K=12 nodal
            // values around the peak, so spread_norm capped around 0.5
            // even though the per-element value was 270 MPa above the
            // smoothed average. delta directly measures "is sharp telling
            // us something smooth missed", which is what peak preservation
            // actually wants.
            const double BLEND_MIN = 0.05;
            const double BLEND_MAX = 0.20;

            // Hotspot support radius for the sharp path's max-pool.
            //
            // The earlier sharp implementation took the closest-point-on-tri
            // nearest single triangle's parent VON. That painted high stress
            // only at the CAD vertices geometrically closest to a peak
            // surface element — its neighbours got their own nearest tri's
            // VON, which on a curved hole-edge boundary is often a different
            // (lower-stress) element. Result: the hotspot fragmented into
            // isolated red/orange islands instead of a continuous patch.
            //
            // To make the high-stress region contiguous, the sharp value at
            // each CAD vertex is now the MAX parent-element VON over every
            // surface tri whose closest-point distance is within the support
            // radius. A peak element bleeds into all CAD vertices within the
            // radius, producing a coherent patch around the concentration.
            //
            // Radius = 1.5 × mean FE node spacing keeps the bleed local
            // (about one FE element away in each direction) — wide enough
            // to merge adjacent hotspot islands but tight enough that
            // distant low-stress elements never inherit a high value. The
            // existing α blend keeps the sharp influence gated to regions
            // where sharp - smooth is meaningful, so the max-pool only
            // surfaces in the actual concentration zone.
            double sharpSupportRadiusM = Math.Max(index.MeanSpacing * 1.5, 1e-6);
            double sharpSupportRadiusSq = sharpSupportRadiusM * sharpSupportRadiusM;

            // Pre-compute the same colour range used by MapStressToColors so
            // the blend's spread normalisation matches the legend's anchor.
            double preColorMinPa = (summary.MinVonMisesMpa > 0 && summary.MaxVonMisesMpa > 0)
                ? summary.MinVonMisesMpa * 1e6
                : 0.0;
            double preColorMaxPa = (summary.MaxVonMisesMpa > 0)
                ? summary.MaxVonMisesMpa * 1e6
                : 1.0;
            double colorRangePa = Math.Max(preColorMaxPa - preColorMinPa, 1e-6);

            // Sharp-path index: per-FE-surface-triangle centroids carrying
            // their parent element's raw solver VON. Only used when the
            // call site supplied a non-empty feTriVon array (it builds it
            // from perElemVon, so unsupported COSMOSWorks builds will
            // simply leave the sharp path disabled and the result reverts
            // to pure Gaussian smoothing).
            bool useSharpPerElement = feTriIndices != null
                && feTriCentroids != null
                && feTriVon != null
                && feTriVon.Length > 0
                && feTriIndices.Length == feTriVon.Length * 3
                && feTriCentroids.Length == feTriVon.Length * 3;
            int feTriCount = useSharpPerElement ? feTriVon.Length : 0;
            SurfaceNodeSpatialIndex triIndex = useSharpPerElement
                ? new SurfaceNodeSpatialIndex(feTriCentroids)
                : null;

            string stressProjectionMode = useSharpPerElement
                ? "surface_nodal_gaussian_with_neighborhood_peak_preservation"
                : "surface_nodal_local_gaussian_to_display";
            int stressProjectionK = STRESS_K;
            string stressSmoothingMethod = "gaussian";
            string stressSmoothingSource = "fe_surface_nodes";
            string peakPreservationMethod = useSharpPerElement
                ? "per_element_max_in_support_radius_blended"
                : "none";
            string peakBlendSource = useSharpPerElement
                ? "sharp_minus_smooth_normalized_by_color_range"
                : "n/a";

            // Diagnostics across all CAD vertices for the blend
            double sumAlpha = 0;
            int alphaSamples = 0;
            int highAlphaCount = 0;
            // Hotspot-support stats: average / max number of in-radius
            // candidate tris per CAD vertex, so the manifest can record
            // how broad the max-pool actually was on this study.
            long totalSupportContrib = 0;
            int supportSamples = 0;
            int maxSupportContrib = 0;

            for (int i = 0; i < cad.VertexCount; i++)
            {
                double cx = cad.Positions[i * 3];
                double cy = cad.Positions[i * 3 + 1];
                double cz = cad.Positions[i * 3 + 2];

                // ---- Smooth field: Gaussian-weighted K=12 over FE surface nodes ----
                var stressNbrs = index.FindNearestK(cx, cy, cz, STRESS_K);
                if (stressNbrs.Length == 0)
                {
                    cadVertsNoStressNeighbour++;
                }
                else
                {
                    if (stressNbrs[0].dist > maxNeighbourDist) maxNeighbourDist = stressNbrs[0].dist;
                    if (stressNbrs[0].dist > farThreshold) farVerts++;

                    double wSum = 0, sSum = 0;
                    double sNbrMax = double.MinValue, sNbrMin = double.MaxValue;
                    int coveredCount = 0;
                    foreach (var n in stressNbrs)
                    {
                        if (feStressCovered != null && !feStressCovered[n.idx]) continue;
                        coveredCount++;
                        double sV = feSurfaceStress[n.idx];
                        if (sV > sNbrMax) sNbrMax = sV;
                        if (sV < sNbrMin) sNbrMin = sV;
                        if (n.dist > smoothingRadiusM) continue;
                        double w = Math.Exp(-(n.dist * n.dist) * inv2SigmaSq);
                        wSum += w;
                        sSum += w * sV;
                    }

                    double smoothVon = 0;
                    bool haveSmooth = false;
                    if (wSum > 0)
                    {
                        smoothVon = sSum / wSum;
                        haveSmooth = true;
                    }
                    else
                    {
                        // Fallback: nearest covered node so
                        // cad_verts_no_covered_stress_neighbour stays at 0.
                        for (int k = 0; k < stressNbrs.Length; k++)
                        {
                            int candIdx = stressNbrs[k].idx;
                            if (feStressCovered != null && !feStressCovered[candIdx]) continue;
                            smoothVon = feSurfaceStress[candIdx];
                            haveSmooth = true;
                            break;
                        }
                    }

                    if (!haveSmooth)
                    {
                        cadVertsNoStressNeighbour++;
                    }
                    else
                    {
                        // ---- Sharp field: max parent-VON over surface tris within support radius ----
                        // Replaces the prior K=1 nearest-tri behaviour, which
                        // painted high stress only at the CAD vertices
                        // geometrically closest to a peak surface element. On
                        // the curved hole boundary this fragmented the
                        // hotspot into isolated red/orange islands because
                        // each adjacent CAD vertex selected a different
                        // nearest tri.
                        //
                        // For each CAD vertex, gather K=8 nearest centroids
                        // and run closest-point-on-triangle on each. Any tri
                        // whose closest-point distance is within
                        // sharpSupportRadiusM contributes its parent VON to
                        // a max accumulator. The sharp value is that
                        // maximum — a single hotspot element bleeds into
                        // every CAD vertex within the support radius,
                        // producing a contiguous high-stress patch. If no
                        // candidate tri falls inside the radius (CAD vertex
                        // far from any FE surface — rare given the 95% gate
                        // upstream), fall back to the closest tri so the
                        // sharp value is never undefined.
                        const int SHARP_CANDIDATE_K = 8;
                        double sharpVon = smoothVon;
                        int sharpSupportContribCount = 0;
                        if (useSharpPerElement)
                        {
                            var triCandidates = triIndex.FindNearestK(cx, cy, cz, SHARP_CANDIDATE_K);
                            double bestDist2 = double.MaxValue;
                            int bestTri = -1;
                            double maxInRadius = double.NegativeInfinity;
                            foreach (var c in triCandidates)
                            {
                                int t = c.idx;
                                if (t < 0 || t >= feTriCount) continue;
                                int v0 = feTriIndices[t * 3];
                                int v1 = feTriIndices[t * 3 + 1];
                                int v2 = feTriIndices[t * 3 + 2];
                                double u, v, w, d2;
                                ClosestPointOnTriangle(
                                    cx, cy, cz,
                                    feSurfacePositions[v0 * 3], feSurfacePositions[v0 * 3 + 1], feSurfacePositions[v0 * 3 + 2],
                                    feSurfacePositions[v1 * 3], feSurfacePositions[v1 * 3 + 1], feSurfacePositions[v1 * 3 + 2],
                                    feSurfacePositions[v2 * 3], feSurfacePositions[v2 * 3 + 1], feSurfacePositions[v2 * 3 + 2],
                                    out u, out v, out w, out d2);
                                if (d2 < bestDist2)
                                {
                                    bestDist2 = d2;
                                    bestTri = t;
                                }
                                if (d2 <= sharpSupportRadiusSq)
                                {
                                    double v_ = feTriVon[t];
                                    if (v_ > maxInRadius) maxInRadius = v_;
                                    sharpSupportContribCount++;
                                }
                            }
                            if (sharpSupportContribCount > 0)
                            {
                                sharpVon = maxInRadius;
                            }
                            else if (bestTri >= 0)
                            {
                                // Fallback: no tri within support radius
                                // (CAD vertex sits in geometry uncovered by
                                // the FE surface mesh). Use the closest tri
                                // so we never write smoothVon as sharp by
                                // accident.
                                sharpVon = feTriVon[bestTri];
                            }
                            totalSupportContrib += sharpSupportContribCount;
                            if (sharpSupportContribCount > maxSupportContrib)
                                maxSupportContrib = sharpSupportContribCount;
                            supportSamples++;
                        }

                        // ---- Adaptive blend: delta = sharp - smooth, normalised ----
                        // Positive delta means the per-element peak is
                        // hidden by nodal smoothing; that's where we want
                        // sharp to win. Negative or near-zero delta means
                        // smooth already captures the local value, no need
                        // to introduce per-element Voronoi-cell texture.
                        double alpha = 0.0;
                        if (useSharpPerElement)
                        {
                            double delta = sharpVon - smoothVon;
                            if (delta > 0)
                            {
                                double deltaNorm = delta / colorRangePa;
                                if (deltaNorm <= BLEND_MIN)
                                {
                                    alpha = 0.0;
                                }
                                else if (deltaNorm >= BLEND_MAX)
                                {
                                    alpha = 1.0;
                                }
                                else
                                {
                                    double t = (deltaNorm - BLEND_MIN) / (BLEND_MAX - BLEND_MIN);
                                    alpha = t * t * (3.0 - 2.0 * t); // smoothstep
                                }
                            }
                            sumAlpha += alpha;
                            alphaSamples++;
                            if (alpha > 0.5) highAlphaCount++;
                        }

                        double finalVon = (1.0 - alpha) * smoothVon + alpha * sharpVon;
                        cadStress[i] = finalVon;
                        if (finalVon > maxStress) { maxStress = finalVon; peakIdx = i; }
                    }
                }

                // ---- Displacement: K=4 IDW over FE surface nodes (unchanged) ----
                if (feSurfaceHasDisplacement)
                {
                    var dispNbrs = index.FindNearestK(cx, cy, cz, K);
                    double dWeight = 0, sDx = 0, sDy = 0, sDz = 0;
                    foreach (var n in dispNbrs)
                    {
                        if (feDispCovered != null && !feDispCovered[n.idx]) continue;
                        double w = 1.0 / Math.Max(n.dist * n.dist, 1e-18);
                        dWeight += w;
                        sDx += w * feSurfaceDispX[n.idx];
                        sDy += w * feSurfaceDispY[n.idx];
                        sDz += w * feSurfaceDispZ[n.idx];
                    }
                    if (dWeight > 0)
                    {
                        cadDispX[i] = sDx / dWeight;
                        cadDispY[i] = sDy / dWeight;
                        cadDispZ[i] = sDz / dWeight;
                    }
                    else
                    {
                        cadVertsNoDispNeighbour++;
                    }
                }
            }

            // Colour mapping: solver-reported [min, max] VON when available
            // (matches the SolidWorks legend); fall back to [0, observed peak]
            // when min is missing (e.g. older API layout).
            double colorMinPa = (summary.MinVonMisesMpa > 0 && summary.MaxVonMisesMpa > 0)
                ? summary.MinVonMisesMpa * 1e6
                : 0.0;
            double colorMaxPa = (summary.MaxVonMisesMpa > 0)
                ? summary.MaxVonMisesMpa * 1e6
                : (maxStress > 0 ? maxStress : 1.0);
            byte[] cadColors = MapStressToColors(cadStress, cad.VertexCount, colorMinPa, colorMaxPa);

            float[] morphPositions = null;
            float[] morphNormals = null;
            float[] morphBoundsMin = null;
            float[] morphBoundsMax = null;
            if (feSurfaceHasDisplacement)
            {
                morphPositions = new float[cad.VertexCount * 3];
                float mMinX = float.MaxValue, mMinY = float.MaxValue, mMinZ = float.MaxValue;
                float mMaxX = float.MinValue, mMaxY = float.MinValue, mMaxZ = float.MinValue;
                for (int i = 0; i < cad.VertexCount; i++)
                {
                    float dx = (float)cadDispX[i], dy = (float)cadDispY[i], dz = (float)cadDispZ[i];
                    morphPositions[i * 3 + 0] = dx;
                    morphPositions[i * 3 + 1] = dy;
                    morphPositions[i * 3 + 2] = dz;
                    if (dx < mMinX) mMinX = dx; if (dx > mMaxX) mMaxX = dx;
                    if (dy < mMinY) mMinY = dy; if (dy > mMaxY) mMaxY = dy;
                    if (dz < mMinZ) mMinZ = dz; if (dz > mMaxZ) mMaxZ = dz;
                }
                morphBoundsMin = new float[] { mMinX, mMinY, mMinZ };
                morphBoundsMax = new float[] { mMaxX, mMaxY, mMaxZ };

                float[] deformedPos = new float[cad.VertexCount * 3];
                for (int i = 0; i < deformedPos.Length; i++)
                    deformedPos[i] = cad.Positions[i] + morphPositions[i];
                float[] deformedNormals = ComputeVertexNormals(deformedPos, cad.Indices, cad.VertexCount);
                morphNormals = new float[deformedNormals.Length];
                for (int i = 0; i < morphNormals.Length; i++)
                    morphNormals[i] = deformedNormals[i] - cad.Normals[i];
            }

            // Bounding box
            float bbMinX = float.MaxValue, bbMinY = float.MaxValue, bbMinZ = float.MaxValue;
            float bbMaxX = float.MinValue, bbMaxY = float.MinValue, bbMaxZ = float.MinValue;
            for (int i = 0; i < cad.Positions.Length; i += 3)
            {
                if (cad.Positions[i] < bbMinX) bbMinX = cad.Positions[i];
                if (cad.Positions[i] > bbMaxX) bbMaxX = cad.Positions[i];
                if (cad.Positions[i + 1] < bbMinY) bbMinY = cad.Positions[i + 1];
                if (cad.Positions[i + 1] > bbMaxY) bbMaxY = cad.Positions[i + 1];
                if (cad.Positions[i + 2] < bbMinZ) bbMinZ = cad.Positions[i + 2];
                if (cad.Positions[i + 2] > bbMaxZ) bbMaxZ = cad.Positions[i + 2];
            }

            // Sanity: warn if the CAD bbox vs FE bbox suggest a coordinate-frame mismatch.
            // (Purely advisory; a real mismatch would also break the projection itself.)
            float feCx = 0, feCy = 0, feCz = 0;
            for (int i = 0; i < feCount; i++)
            {
                feCx += feSurfacePositions[i * 3];
                feCy += feSurfacePositions[i * 3 + 1];
                feCz += feSurfacePositions[i * 3 + 2];
            }
            feCx /= feCount; feCy /= feCount; feCz /= feCount;
            float cadCx = 0.5f * (bbMinX + bbMaxX);
            float cadCy = 0.5f * (bbMinY + bbMaxY);
            float cadCz = 0.5f * (bbMinZ + bbMaxZ);
            double centroidShift = Math.Sqrt(
                (feCx - cadCx) * (feCx - cadCx) +
                (feCy - cadCy) * (feCy - cadCy) +
                (feCz - cadCz) * (feCz - cadCz));
            if (centroidShift > index.MeanSpacing * 5)
            {
                Warn(summary, $"Stage 3: CAD vs FE centroid shift {centroidShift:G4} m is large relative to mean FE spacing {index.MeanSpacing:G4} m. Check coordinate frames.");
            }

            double farPct = cad.VertexCount > 0 ? 100.0 * farVerts / cad.VertexCount : 0;
            int feDispCoveredCount = 0;
            if (feDispCovered != null)
            {
                for (int i = 0; i < feDispCovered.Length; i++)
                    if (feDispCovered[i]) feDispCoveredCount++;
            }
            string stressRangeSource = (summary.MinVonMisesMpa > 0 && summary.MaxVonMisesMpa > 0)
                ? "solver_minmax"
                : "observed_max";
            summary.CadProjectionDiagnostic = new CadProjectionDiagnosticInfo
            {
                CadVertexCount = cad.VertexCount,
                CadTriangleCount = cad.TriangleCount,
                BaseCadVertexCount = cadBase.VertexCount,
                BaseCadTriangleCount = cadBase.TriangleCount,
                RefinedCadVertexCount = cad.VertexCount,
                RefinedCadTriangleCount = cad.TriangleCount,
                RefinementMethod = refinementMethod,
                RefinementEdgeThresholdM = refinementEdgeThresholdM,
                RefinementPasses = refinementPasses,
                RefinementTriangleSplits = refinementSplits,
                FeSurfaceNodeCount = feCount,
                FeStressCoveredCount = feStressCoveredCount,
                FeDispCoveredCount = feDispCoveredCount,
                K = K,
                Weighting = "inverse_distance_squared",
                MaxNeighbourDistanceM = maxNeighbourDist,
                MeanFeNodeSpacingM = index.MeanSpacing,
                FarVertexFractionPct = farPct,
                CadVertsNoStressNeighbour = cadVertsNoStressNeighbour,
                CadVertsNoDispNeighbour = cadVertsNoDispNeighbour,
                Approximate = true,
                StressProjectionMode = stressProjectionMode,
                StressProjectionK = stressProjectionK,
                StressColorMapping = "min-max-linear",
                StressRangeSource = stressRangeSource,
                ColorRangeMinPa = colorMinPa,
                ColorRangeMaxPa = colorMaxPa,
                StressSmoothingMethod = stressSmoothingMethod,
                StressSmoothingSource = stressSmoothingSource,
                StressSmoothingSigmaM = sigma,
                StressSmoothingRadiusM = smoothingRadiusM,
                StressPeakPreservationMethod = peakPreservationMethod,
                StressPeakBlendSource = peakBlendSource,
                StressPeakBlendMin = useSharpPerElement ? BLEND_MIN : 0.0,
                StressPeakBlendMax = useSharpPerElement ? BLEND_MAX : 0.0,
                StressPeakBlendMeanAlpha = alphaSamples > 0 ? sumAlpha / alphaSamples : 0.0,
                StressPeakBlendHighAlphaPct = cad.VertexCount > 0
                    ? 100.0 * highAlphaCount / cad.VertexCount
                    : 0.0,
                DisplayPeakStressPa = (peakIdx >= 0 && peakIdx < cad.VertexCount)
                    ? cadStress[peakIdx]
                    : 0.0,
                DisplayPeakStressMpa = (peakIdx >= 0 && peakIdx < cad.VertexCount)
                    ? cadStress[peakIdx] / 1e6
                    : 0.0,
                DisplayPeakVertexIndex = peakIdx,
                DisplayColorRangeMinPa = colorMinPa,
                DisplayColorRangeMaxPa = colorMaxPa,
                DisplayColorRangeSource = stressRangeSource,
                DisplayScalarFieldMode = stressProjectionMode,
                DisplayScalarFieldSource = useSharpPerElement
                    ? "(1-alpha)*gaussian_smooth_nodal + alpha*per_element_max_within_support_sharp; alpha=smoothstep(BLEND_MIN, BLEND_MAX, max(0, sharp - smooth) / colorRange); sharp = max(parent_VON over surface tris with closest-point distance <= sharpSupportRadiusM)"
                    : "gaussian_smooth_nodal",
                // Sidecar filename is set at the caller after the file is
                // actually written (the function name uses studySlug which
                // isn't visible from inside this method).
                DisplayScalarsSidecar = null,
                DisplayHotspotRefinementMethod = useSharpPerElement
                    ? "max_parent_von_within_support_radius"
                    : "none",
                DisplayHotspotSupportRadiusM = useSharpPerElement ? sharpSupportRadiusM : 0.0,
                DisplayHotspotSupportRadiusRatio = (useSharpPerElement && index.MeanSpacing > 0)
                    ? sharpSupportRadiusM / index.MeanSpacing
                    : 0.0,
                DisplayHotspotSupportCountMean = supportSamples > 0
                    ? (double)totalSupportContrib / supportSamples
                    : 0.0,
                DisplayHotspotSupportCountMax = maxSupportContrib,
            };
            double meanAlpha = alphaSamples > 0 ? sumAlpha / alphaSamples : 0.0;
            double highAlphaPct = cad.VertexCount > 0
                ? 100.0 * highAlphaCount / cad.VertexCount
                : 0.0;
            double meanSupportContrib = supportSamples > 0
                ? (double)totalSupportContrib / supportSamples
                : 0.0;
            Console.WriteLine($"    FEA Stage 3: projected {cad.VertexCount:N0} CAD verts via {stressProjectionMode} (k={stressProjectionK}, sigma={sigma * 1000:F3} mm = {(sigma / Math.Max(index.MeanSpacing, 1e-12)):F2}× mean FE spacing, radius={smoothingRadiusM * 1000:F3} mm, peak={peakPreservationMethod}, support_radius={sharpSupportRadiusM * 1000:F3} mm = {(useSharpPerElement && index.MeanSpacing > 0 ? sharpSupportRadiusM / index.MeanSpacing : 0.0):F2}× mean FE spacing, support_count_mean={meanSupportContrib:F2}/8, support_count_max={maxSupportContrib}, blend [{(useSharpPerElement ? BLEND_MIN : 0.0):F2}, {(useSharpPerElement ? BLEND_MAX : 0.0):F2}], mean alpha={meanAlpha:F3}, sharp-dominated verts={highAlphaPct:F2}%); stress covered {feStressCoveredCount}/{feCount}, disp covered {feDispCoveredCount}/{feCount}, max neighbour dist {maxNeighbourDist:G4} m, far-vert frac {farPct:F2}%, CAD verts w/o covered stress neighbour {cadVertsNoStressNeighbour}, w/o covered disp neighbour {cadVertsNoDispNeighbour}");

            // Capture the CAD display-mesh peak as a *display-only* diagnostic
            // — never let it shadow solver-authoritative fields:
            //   * summary.MaxStressLocation (drives results.max_von_mises_location_m
            //     and summary.max_stress_location) must stay tied to the solver
            //     peak set earlier from GetMinMaxStress / .OUT fallback.
            //   * summary.MaxVonMisesMpa is left untouched; the IDW-projected
            //     peak is always at or below the FE solver peak by construction
            //     (a weighted average can't exceed its largest source value),
            //     so promoting magnitude is unnecessary and risks drift.
            // The CAD peak coordinates land in cad_projection.display_peak_stress_location_m
            // for visualization-side debug only.
            if (maxStress > 0 && peakIdx < cad.VertexCount)
            {
                summary.CadProjectionDiagnostic.DisplayPeakStressLocationM = new double[]
                {
                    cad.Positions[peakIdx * 3],
                    cad.Positions[peakIdx * 3 + 1],
                    cad.Positions[peakIdx * 3 + 2]
                };
            }

            // Display-mesh peak (the brightest CAD vertex). May be lower
            // than summary.MaxVonMisesMpa: the solver per-element peak can
            // live in an interior element that doesn't paint any surface
            // vertex, so this is recorded as a separate display fact.
            double displayPeakStressPa = (peakIdx >= 0 && peakIdx < cad.VertexCount)
                ? cadStress[peakIdx]
                : 0.0;
            double[] displayPeakLoc = (peakIdx >= 0 && peakIdx < cad.VertexCount)
                ? new double[]
                {
                    cad.Positions[peakIdx * 3],
                    cad.Positions[peakIdx * 3 + 1],
                    cad.Positions[peakIdx * 3 + 2]
                }
                : null;
            string displayScalarFieldSource = useSharpPerElement
                ? "(1-alpha)*gaussian_smooth_nodal + alpha*per_element_max_within_support_sharp; alpha=smoothstep(BLEND_MIN, BLEND_MAX, max(0, sharp - smooth) / colorRange); sharp = max(parent_VON over surface tris with closest-point distance <= sharpSupportRadiusM)"
                : "gaussian_smooth_nodal";

            return new FeaMeshData
            {
                Positions = cad.Positions,
                Normals = cad.Normals,
                Indices = cad.Indices,
                VertexColors = cadColors,
                MorphPositions = morphPositions,
                MorphNormals = morphNormals,
                BoundsMin = new float[] { bbMinX, bbMinY, bbMinZ },
                BoundsMax = new float[] { bbMaxX, bbMaxY, bbMaxZ },
                MorphBoundsMin = morphBoundsMin,
                MorphBoundsMax = morphBoundsMax,
                VertexCount = cad.VertexCount,
                TriangleCount = cad.TriangleCount,
                // UniqueNodeCount keeps its solver/FE-surface meaning even on
                // the CAD-projected path, so summary.SurfaceNodeCount, which
                // consumers read as the FE surface-node count, doesn't get
                // overloaded with the refined display-mesh vertex count.
                // Refined display counts are reported under cad_projection.*
                // (cad_vertex_count, refined_cad_vertex_count, etc.).
                UniqueNodeCount = feCount,
                // Display-side data the app needs to render its own contour
                // bands / legend without re-deriving stress from the baked
                // vertex colours. Length parallels Positions (one scalar per
                // CAD vertex). Written to a sidecar JSON next to the GLB.
                DisplayVertexStressPa = cadStress,
                DisplayPeakVertexIndex = peakIdx,
                DisplayPeakStressPa = displayPeakStressPa,
                DisplayPeakStressLocationM = displayPeakLoc,
                DisplayColorRangeMinPa = colorMinPa,
                DisplayColorRangeMaxPa = colorMaxPa,
                DisplayColorRangeSource = stressRangeSource,
                DisplayScalarFieldMode = stressProjectionMode,
                DisplayScalarFieldSource = displayScalarFieldSource,
            };
        }

        /// <summary>
        /// Standard glTF type-shape coercion (object[]/double[]/float[] → float[]).
        /// Mirrors GlbExporter's helper to avoid coupling the two files.
        /// </summary>
        private float[] ConvertToFloatArray(object obj)
        {
            if (obj == null) return null;
            if (obj is float[] f) return f;
            if (obj is double[] d) { var r = new float[d.Length]; for (int i = 0; i < d.Length; i++) r[i] = (float)d[i]; return r; }
            if (obj is object[] arr)
            {
                var r = new float[arr.Length];
                for (int i = 0; i < arr.Length; i++)
                    r[i] = arr[i] != null ? Convert.ToSingle(arr[i]) : 0f;
                return r;
            }
            return null;
        }

