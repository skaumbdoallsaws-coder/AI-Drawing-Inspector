    /// <summary>
    /// Extracts FEA simulation results from SolidWorks Simulation studies.
    /// Produces a stress-colored GLB with morph target deformation, a results JSON summary,
    /// and a per-run provenance manifest.
    ///
    /// Requirements:
    ///   - SolidWorks Premium or Simulation license
    ///   - At least one completed static study with saved results (when no explicit
    ///     --fea-study-name / --fea-study-index is supplied; explicit selection
    ///     hard-rejects non-static or unsolved studies — see SelectStudy).
    ///
    /// Output filename contract (always slug-suffixed, even when selection is implicit,
    /// so multiple studies on the same part never overwrite each other):
    ///   - {partNumber}_fea_{studySlug}.glb            Stress-colored surface mesh + morph target
    ///   - {partNumber}_fea_{studySlug}_results.json   Result summary
    ///   - {partNumber}_fea_{studySlug}_manifest.json  Provenance manifest (audit record)
    ///
    /// The slug is generated by MakeStudySlug() and is a deterministic, filesystem-safe
    /// transformation of the SolidWorks study name (lowercase, hyphen-preserving, 64-char
    /// cap, falls back to "study_{index}" when the name has no surviving alphanumerics).
    /// </summary>
    public class SimulationExtractor
