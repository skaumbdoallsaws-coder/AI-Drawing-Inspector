        /// <summary>
        /// Extract FEA simulation results with explicit study selection.
        /// Selection precedence:
        ///   1. studyName (exact, case-insensitive) if provided
        ///   2. studyIndex (0-based) if &gt;= 0
        ///   3. Implicit: first completed static study (legacy behavior — logs a notice)
        /// </summary>
        /// <param name="swApp">SolidWorks application instance</param>
        /// <param name="modelDoc">Open part document</param>
        /// <param name="outputFolder">Directory to write output files</param>
        /// <param name="partNumber">Part number for output filenames</param>
        /// <param name="studyName">Optional study name to force selection</param>
        /// <param name="studyIndex">Optional 0-based study index to force selection (-1 = unset)</param>
        /// <returns>Path to the GLB file, or null on failure</returns>
        public string ExtractSimulation(ISldWorks swApp, IModelDoc2 modelDoc, string outputFolder, string partNumber,
            string studyName, int studyIndex)
        {
            if (swApp == null || modelDoc == null)
            {
                Console.WriteLine("    FEA Warning: No application or document for simulation extraction");
                return null;
            }

            if (!Directory.Exists(outputFolder))
                Directory.CreateDirectory(outputFolder);

            Console.WriteLine("    FEA: Accessing Simulation add-in...");

            // Access the Simulation add-in via IModelDocExtension
            object cosAddinObj = null;
            ICosmosWorks cosWorks = null;
            dynamic studyMgr = null;
            dynamic study = null;

            try
            {
                // Get the SOLIDWORKS Simulation (CosmosWorks) add-in
                cosAddinObj = swApp.GetAddInObject("SldWorks.Simulation");
                if (cosAddinObj == null)
                {
                    Console.WriteLine("    FEA Error: Could not access Simulation add-in. Is Simulation license active?");
                    return null;
                }

                // The add-in object exposes a CosmosWorks property; use dynamic for late-bound access
                cosWorks = (ICosmosWorks)((dynamic)cosAddinObj).CosmosWorks;
                if (cosWorks == null)
                {
                    Console.WriteLine("    FEA Error: Could not get CosmosWorks interface");
                    return null;
                }

                // Get the active document in Simulation context
                CWModelDoc cwDoc = (CWModelDoc)cosWorks.ActiveDoc;
                if (cwDoc == null)
                {
                    Console.WriteLine("    FEA Error: No active document in Simulation context");
                    return null;
                }

                studyMgr = ((dynamic)cwDoc).StudyManager;
                if (studyMgr == null)
                {
                    Console.WriteLine("    FEA Error: No study manager available");
                    return null;
                }

                int studyCount = studyMgr.StudyCount;
                Console.WriteLine($"    FEA: Found {studyCount} simulation studies");

                if (studyCount == 0)
                {
                    Console.WriteLine("    FEA Warning: No simulation studies found in this document");
                    return null;
                }

                // Pick the study. Honor explicit name first, then explicit index,
                // then fall back to the legacy implicit "first completed static" path.
                study = SelectStudy(studyMgr, studyCount, studyName, studyIndex, modelDoc);
                if (study == null)
                {
                    Console.WriteLine("    FEA Warning: No suitable study found for the requested selection");
                    return null;
                }

                string selectedStudyName = study.Name as string ?? "Unknown Study";
                Console.WriteLine($"    FEA: Using study '{selectedStudyName}'");

                // Extract study metadata
                var summary = ExtractStudyMetadata(study, selectedStudyName);

                // Configuration name comes from the SolidWorks document, not from the
                // study, so set it here where modelDoc is in scope.
                try
                {
                    var configMgr = modelDoc.ConfigurationManager;
                    if (configMgr != null)
                    {
                        var activeConfig = configMgr.ActiveConfiguration;
                        if (activeConfig != null)
                            summary.ConfigurationName = ((dynamic)activeConfig).Name as string;
                    }
                }
                catch (Exception ex)
                {
                    Warn(summary, "Configuration name not accessible: " + ex.Message);
                }

                // Extract surface mesh with stress and displacement data
                var meshData = ExtractResultMesh(study, modelDoc, summary);
                if (meshData == null)
                {
                    Console.WriteLine("    FEA Error: Solver-backed visualization mesh unavailable.");
                    Console.WriteLine("               Refusing to write a normal FEA GLB from approximate CAD tessellation.");
                    return null;
                }

                summary.SurfaceNodeCount = meshData.UniqueNodeCount > 0 ? meshData.UniqueNodeCount : meshData.VertexCount;
                summary.HasMorphTarget = meshData.MorphPositions != null && meshData.MorphPositions.Length > 0;
                summary.VisualizationMode = "solver_fe_mesh";
                summary.VisualizationApproximate = false;

                // Compute safety factor
                if (summary.YieldStrengthMpa > 0 && summary.MaxVonMisesMpa > 0)
                {
                    summary.SafetyFactor = Math.Round(summary.YieldStrengthMpa / summary.MaxVonMisesMpa, 2);
                }

                Console.WriteLine($"    FEA: Surface mesh: {meshData.VertexCount:N0} vertices, {meshData.TriangleCount:N0} triangles");
                Console.WriteLine($"    FEA: Max von Mises: {summary.MaxVonMisesMpa:F3} MPa");
                Console.WriteLine($"    FEA: Max displacement: {summary.MaxDisplacementMm:F5} mm");
                Console.WriteLine($"    FEA: Material: {summary.MaterialName ?? "N/A"}, Yield: {summary.YieldStrengthMpa:F1} MPa");
                Console.WriteLine($"    FEA: Safety factor: {summary.SafetyFactor:F2}");
                Console.WriteLine($"    FEA: Morph target: {(summary.HasMorphTarget ? "yes" : "no")}");

                // Determine the actual study index (helpful for the manifest when the
                // user picked by name and we want to record the resolved index).
                int resolvedStudyIndex = ResolveStudyIndex(studyMgr, studyCount, selectedStudyName);

                // Compute a deterministic, filesystem-safe slug from the study name.
                // The slug is appended to every output filename so multiple studies on
                // the same part never overwrite each other.
                string studySlug = MakeStudySlug(selectedStudyName, resolvedStudyIndex);

                string baseName = partNumber + "_fea_" + studySlug;
                string glbPath = Path.Combine(outputFolder, baseName + ".glb");
                string jsonPath = Path.Combine(outputFolder, baseName + "_results.json");
                string manifestPath = Path.Combine(outputFolder, baseName + "_manifest.json");

                // Build and write GLB
                WriteFeaGlb(meshData, glbPath);
                long glbSizeKb = new FileInfo(glbPath).Length / 1024;
                Console.WriteLine($"    FEA: GLB written: {Path.GetFileName(glbPath)} ({glbSizeKb} KB)");

                // Write results JSON
                WriteFeaResultsJson(summary, jsonPath);
                Console.WriteLine($"    FEA: Results JSON written: {Path.GetFileName(jsonPath)}");

                // Write provenance manifest. selectionMode reflects how the study was chosen.
                string selectionMode;
                if (!string.IsNullOrWhiteSpace(studyName)) selectionMode = "explicit-name";
                else if (studyIndex >= 0) selectionMode = "explicit-index";
                else selectionMode = "implicit";

                WriteFeaManifest(
                    manifestPath: manifestPath,
                    swApp: swApp,
                    modelDoc: modelDoc,
                    partNumber: partNumber,
                    studyName: selectedStudyName,
                    studySlug: studySlug,
                    studyIndex: resolvedStudyIndex,
                    selectionMode: selectionMode,
                    summary: summary,
                    outputFiles: new[] {
                        Path.GetFileName(glbPath),
                        Path.GetFileName(jsonPath),
                        Path.GetFileName(manifestPath)
                    });
                Console.WriteLine($"    FEA: Manifest written: {Path.GetFileName(manifestPath)}");

                return glbPath;
            }
            catch (Exception ex)
            {
                Console.WriteLine($"    FEA Error: {ex.Message}");
                Console.WriteLine($"    FEA Stack: {ex.StackTrace}");
                return null;
            }
            finally
            {
                // Release COM objects in reverse order
                SafeReleaseCom(study);
                SafeReleaseCom(studyMgr);
                SafeReleaseCom(cosWorks);
                SafeReleaseCom(cosAddinObj);
            }
        }

        /// <summary>
        /// Pick a study using explicit selection (name, then index) and only fall back to the
        /// legacy "first completed static" search when neither is supplied. The legacy fallback
        /// emits an "implicit selection" notice so callers can see they took the default path.
        ///
        /// Explicit selection (name/index) hard-rejects studies that are not static or that
        /// have no accessible results, since the extraction pipeline downstream assumes a
        /// completed static study. This avoids silent misleading output from the wrong study.
        /// </summary>
        private dynamic SelectStudy(dynamic studyMgr, int studyCount, string studyName, int studyIndex, IModelDoc2 modelDoc)
        {
            // 1) Explicit name (case-insensitive)
            if (!string.IsNullOrWhiteSpace(studyName))
            {
                for (int i = 0; i < studyCount; i++)
                {
                    dynamic candidate = null;
                    try
                    {
                        candidate = studyMgr.GetStudy(i);
                        if (candidate == null) continue;
                        string name = null;
                        try { name = candidate.Name as string; } catch { }
                        if (!string.IsNullOrEmpty(name)
                            && string.Equals(name, studyName, StringComparison.OrdinalIgnoreCase))
                        {
                            string reason;
                            if (!IsStaticAndSolved(candidate, modelDoc, out reason))
                            {
                                Console.WriteLine($"    FEA Error: Study '{name}' (index {i}) rejected: {reason}");
                                Console.WriteLine($"               Explicit selection requires a static study with accessible results.");
                                SafeReleaseCom(candidate);
                                return null;
                            }
                            Console.WriteLine($"    FEA: Selected study '{name}' by name (index {i})");
                            return candidate;
                        }
                    }
                    catch (Exception ex)
                    {
                        Console.WriteLine($"    FEA: Error inspecting study {i} for name match: {ex.Message}");
                    }
                    SafeReleaseCom(candidate);
                }
                Console.WriteLine($"    FEA Error: No study named '{studyName}' was found in this document");
                return null;
            }

            // 2) Explicit index
            if (studyIndex >= 0)
            {
                if (studyIndex >= studyCount)
                {
                    Console.WriteLine($"    FEA Error: --fea-study-index {studyIndex} out of range (0..{studyCount - 1})");
                    return null;
                }
                dynamic candidate = null;
                try
                {
                    candidate = studyMgr.GetStudy(studyIndex);
                    if (candidate == null)
                    {
                        Console.WriteLine($"    FEA Error: GetStudy({studyIndex}) returned null");
                        return null;
                    }
                    string name = null;
                    try { name = candidate.Name as string; } catch { }
                    string reason;
                    if (!IsStaticAndSolved(candidate, modelDoc, out reason))
                    {
                        Console.WriteLine($"    FEA Error: Study '{name ?? "(unnamed)"}' at index {studyIndex} rejected: {reason}");
                        Console.WriteLine($"               Explicit selection requires a static study with accessible results.");
                        SafeReleaseCom(candidate);
                        return null;
                    }
                    Console.WriteLine($"    FEA: Selected study '{name ?? "(unnamed)"}' by index {studyIndex}");
                    return candidate;
                }
                catch (Exception ex)
                {
                    Console.WriteLine($"    FEA Error: Could not retrieve study at index {studyIndex}: {ex.Message}");
                    SafeReleaseCom(candidate);
                    return null;
                }
            }

            // 3) Legacy implicit fallback — emit a clear notice so users know they hit the default
            Console.WriteLine("    FEA: No --fea-study-name or --fea-study-index supplied; using IMPLICIT selection (first completed static study).");
            return FindCompletedStaticStudy(studyMgr, studyCount, modelDoc);
        }

        /// <summary>
        /// Validate that a study is static (AnalysisType == 0) and has accessible results.
        /// Sets <paramref name="reason"/> to a human-readable rejection reason on failure.
        /// </summary>
        private bool IsStaticAndSolved(dynamic study, IModelDoc2 modelDoc, out string reason)
        {
            reason = null;
            int analysisType;
            try
            {
                analysisType = (int)study.AnalysisType;
            }
            catch (Exception ex)
            {
                reason = "could not read AnalysisType: " + ex.Message;
                return false;
            }

            if (analysisType != 0)
            {
                reason = $"analysis type is '{LabelAnalysisType(analysisType)}' (need 'static')";
                return false;
            }

            string resultsNote;
            if (EnsureResultsAvailable(study, modelDoc, out resultsNote))
                return true;

            reason = string.IsNullOrWhiteSpace(resultsNote)
                ? "no results available - study has not been solved"
                : "no results available - " + resultsNote;
            return false;
        }

        private bool EnsureResultsAvailable(dynamic study, IModelDoc2 modelDoc, out string note)
        {
            note = null;

            string initialError;
            if (TryGetResults(study, out initialError))
                return true;

            string connectNote;
            if (TryConnectExistingAnalysisDatabase(study, modelDoc, out connectNote))
            {
                if (!string.IsNullOrWhiteSpace(connectNote))
                    Console.WriteLine("    FEA: " + connectNote);
                return true;
            }

            note = !string.IsNullOrWhiteSpace(connectNote)
                ? connectNote
                : (!string.IsNullOrWhiteSpace(initialError) ? "results not accessible: " + initialError : null);
            return false;
        }

        private bool TryGetResults(dynamic study, out string error)
        {
            error = null;
            try
            {
                ICWResults results = (ICWResults)study.Results;
                bool ok = results != null;
                SafeReleaseCom(results);
                return ok;
            }
            catch (Exception ex)
            {
                error = ex.Message;
                return false;
            }
        }

        private bool TryConnectExistingAnalysisDatabase(dynamic study, IModelDoc2 modelDoc, out string note)
        {
            note = null;
            string docPath = null;
            try
            {
                if (modelDoc != null)
                    docPath = modelDoc.GetPathName();
            }
            catch { }
            if (string.IsNullOrWhiteSpace(docPath))
                return false;

            string docDir = Path.GetDirectoryName(docPath);
            if (string.IsNullOrWhiteSpace(docDir))
                return false;

            string resultsDir = Path.Combine(docDir, "Simulation Results");
            if (!Directory.Exists(resultsDir))
                return false;

            try
            {
                int rc = (int)study.ConnectAnalysisDatabase(resultsDir);
                string afterError;
                if (TryGetResults(study, out afterError))
                {
                    note = $"Connected existing analysis database: {resultsDir} (rc={rc})";
                    return true;
                }

                note = !string.IsNullOrWhiteSpace(afterError)
                    ? $"ConnectAnalysisDatabase({resultsDir}) rc={rc}, but results are still unavailable: {afterError}"
                    : $"ConnectAnalysisDatabase({resultsDir}) rc={rc}, but results are still unavailable";
            }
            catch (Exception ex)
            {
                note = $"ConnectAnalysisDatabase({resultsDir}) failed: {ex.Message}";
            }

            return false;
        }

        /// <summary>
        /// Find the first completed static study in the study manager.
        /// Prefers studies that have results already computed.
        /// </summary>
        private dynamic FindCompletedStaticStudy(dynamic studyMgr, int studyCount, IModelDoc2 modelDoc)
        {
            for (int i = 0; i < studyCount; i++)
            {
                dynamic candidate = null;
                try
                {
                    candidate = studyMgr.GetStudy(i);
                    if (candidate == null) continue;

                    // Check if it's a static study
                    // swsAnalysisStudyType_e.swsAnalysisStudyTypeStatic = 0
                    int studyType = (int)candidate.AnalysisType;
                    if (studyType != 0)
                    {
                        Console.WriteLine($"    FEA: Skipping study '{candidate.Name}' (type={studyType}, not static)");
                        SafeReleaseCom(candidate);
                        continue;
                    }

                    string resultsNote;
                    if (!EnsureResultsAvailable(candidate, modelDoc, out resultsNote))
                    {
                        Console.WriteLine($"    FEA: Study '{candidate.Name}' has no results object{(string.IsNullOrWhiteSpace(resultsNote) ? "" : ": " + resultsNote)}");
                        SafeReleaseCom(candidate);
                        continue;
                    }

                    Console.WriteLine($"    FEA: Found completed static study '{candidate.Name}' at index {i}");
                    return candidate;
                }
                catch (Exception ex)
                {
                    Console.WriteLine($"    FEA: Error accessing study {i}: {ex.Message}");
                    SafeReleaseCom(candidate);
                }
            }

            return null;
        }

        /// <summary>
        /// Extract study metadata: material, yield strength, mesh info, result extremes.
        /// </summary>
        private FeaResultsSummary ExtractStudyMetadata(dynamic study, string studyName)
        {
            var summary = new FeaResultsSummary
            {
                StudyName = studyName,
                StudyType = "static",
                MaxStressLocation = new double[] { 0, 0, 0 }
            };

            // Get mesh info
            try
            {
                ICWMesh mesh = (ICWMesh)study.Mesh;
                if (mesh != null)
                {
                    try
                    {
                        // Capture the total FE mesh node count BEFORE the result-mesh
                        // extractor runs and dedups to surface-only — surface dedup
                        // overwrites SurfaceNodeCount, so this field is the only one
                        // that mirrors the SolidWorks report's "Total Nodes" row.
                        summary.MeshNodeCount = ((dynamic)mesh).NodeCount;
                        summary.ElementCount = ((dynamic)mesh).ElementCount;
                        Console.WriteLine($"    FEA: Mesh has {summary.MeshNodeCount:N0} nodes, {summary.ElementCount:N0} elements");
                    }
                    catch (Exception ex)
                    {
                        Warn(summary, "Could not read mesh info: " + ex.Message);
                    }
                    finally
                    {
                        SafeReleaseCom(mesh);
                    }
                }
            }
            catch { /* mesh info is non-critical */ }

            // Get material properties from the study's solid component
            try
            {
                ICWSolidManager solidMgr = (ICWSolidManager)study.SolidManager;
                if (solidMgr != null)
                {
                    try
                    {
                        int solidCount = solidMgr.ComponentCount;
                        if (solidCount > 0)
                        {
                            int compErrCode = 0;
                            ICWSolidComponent solidComp = (ICWSolidComponent)solidMgr.GetComponentAt(0, out compErrCode);
                            if (solidComp != null)
                            {
                                try
                                {
                                    int bodyErr = 0;
                                    CWSolidBody solidBody = (CWSolidBody)solidComp.GetSolidBodyAt(0, out bodyErr);
                                    if (solidBody != null)
                                    {
                                        try
                                        {
                                            ICWMaterial material = (ICWMaterial)((dynamic)solidBody).GetSolidBodyMaterial();
                                            if (material != null)
                                            {
                                                try
                                                {
                                                    summary.MaterialName = ((dynamic)material).MaterialName as string;
                                                    PopulateMaterialProperties(material, summary);
                                                }
                                                catch (Exception ex)
                                                {
                                                    Warn(summary, "Could not read material properties: " + ex.Message);
                                                }
                                                finally
                                                {
                                                    SafeReleaseCom(material);
                                                }
                                            }
                                        }
                                        finally
                                        {
                                            SafeReleaseCom(solidBody);
                                        }
                                    }
                                }
                                finally
                                {
                                    SafeReleaseCom(solidComp);
                                }
                            }
                        }
                    }
                    finally
                    {
                        SafeReleaseCom(solidMgr);
                    }
                }
            }
            catch (Exception ex)
            {
                Warn(summary, "Could not extract material info: " + ex.Message);
            }

            // Mesh extras (mesher type label, element sizes, jacobian points, quality stats)
            ExtractMeshExtras(study, summary);

            // Loads & fixtures from the LoadsAndRestraintsManager
            ExtractLoadsAndFixtures(study, summary);

            // Get result extremes (max von Mises, max displacement, reaction forces, strain)
            try
            {
                ICWResults results = (ICWResults)study.Results;
                if (results != null)
                {
                    try
                    {
                        ExtractResultExtremes(results, summary);
                        ExtractStrainExtremes(results, summary);
                    }
                    finally
                    {
                        SafeReleaseCom(results);
                    }
                }
            }
            catch (Exception ex)
            {
                Warn(summary, "Could not extract result extremes: " + ex.Message);
            }

            return summary;
        }

        /// <summary>
        /// Pull additional material properties beyond yield strength.
        /// All values returned in SI by the API; converted to report-friendly units.
        /// Each property fetch is independently wrapped: a missing property leaves the
        /// corresponding summary field null and emits a warning rather than failing.
        /// SolidWorks material database property names: SIGYLD, SIGXT, EX, NUXY, DENS, GXY, ALPX.
        /// </summary>
        private void PopulateMaterialProperties(ICWMaterial material, FeaResultsSummary summary)
        {
            // COSMOSWorks material database values are base SI when queried with
            // the millimeter enum; querying with meters can return display-scaled values.
            int materialDbUnit = (int)swsLinearUnit_e.swsLinearUnitMillimeters;
            int td;

            // Yield (already extracted in v1; re-read here for consistency, value in Pa).
            try
            {
                td = 0;
                double yieldPa = material.GetPropertyByName(materialDbUnit, "SIGYLD", out td);
                if (yieldPa > 0) summary.YieldStrengthMpa = yieldPa / 1e6;
            }
            catch (Exception ex) { Warn(summary, "Material SIGYLD: " + ex.Message); }

            // Tensile strength (Pa → MPa)
            try
            {
                td = 0;
                double tenPa = material.GetPropertyByName(materialDbUnit, "SIGXT", out td);
                if (tenPa > 0) summary.TensileStrengthMpa = tenPa / 1e6;
            }
            catch (Exception ex) { Warn(summary, "Material SIGXT (tensile): " + ex.Message); }

            // Elastic / Young's modulus (Pa → MPa)
            try
            {
                td = 0;
                double exPa = material.GetPropertyByName(materialDbUnit, "EX", out td);
                if (exPa > 0) summary.ElasticModulusMpa = exPa / 1e6;
            }
            catch (Exception ex) { Warn(summary, "Material EX (elastic modulus): " + ex.Message); }

            // Poisson's ratio (dimensionless)
            try
            {
                td = 0;
                double nu = material.GetPropertyByName(materialDbUnit, "NUXY", out td);
                if (nu > 0) summary.PoissonsRatio = nu;
            }
            catch (Exception ex) { Warn(summary, "Material NUXY (Poisson): " + ex.Message); }

            // Mass density (kg/m³ — SolidWorks returns SI)
            try
            {
                td = 0;
                double dens = material.GetPropertyByName(materialDbUnit, "DENS", out td);
                if (dens > 0) summary.MassDensityKgPerM3 = dens;
            }
            catch (Exception ex) { Warn(summary, "Material DENS (density): " + ex.Message); }

            // Shear modulus (Pa → MPa)
            try
            {
                td = 0;
                double gxyPa = material.GetPropertyByName(materialDbUnit, "GXY", out td);
                if (gxyPa > 0) summary.ShearModulusMpa = gxyPa / 1e6;
            }
            catch (Exception ex) { Warn(summary, "Material GXY (shear modulus): " + ex.Message); }

            // Thermal expansion coefficient (per Kelvin)
            try
            {
                td = 0;
                double alpx = material.GetPropertyByName(materialDbUnit, "ALPX", out td);
                if (alpx > 0) summary.ThermalExpansionPerKelvin = alpx;
            }
            catch (Exception ex) { Warn(summary, "Material ALPX (thermal expansion): " + ex.Message); }

            // Material model type — the COSMOSWorks API does not expose a clean string for
            // this. Leave null and warn so it's visible in the manifest.
            Warn(summary, "Material model type (e.g. 'Linear Elastic Isotropic') is not exposed via the COSMOSWorks API; left null.");
        }

        /// <summary>
        /// Best-effort extraction of mesh-related metadata that the SolidWorks report shows
        /// (mesher type, element sizes, Jacobian points, quality stats). Each accessor is
        /// independently wrapped: any missing field is null + warning, never fabricated.
        /// </summary>
        private void ExtractMeshExtras(dynamic study, FeaResultsSummary summary)
        {
            ICWMesh mesh = null;
            try
            {
                mesh = (ICWMesh)study.Mesh;
                if (mesh == null) return;

                // Mesh type label (e.g. "Solid Mesh"). study.MeshType returns an enum int.
                try
                {
                    int meshTypeInt = (int)((dynamic)study).MeshType;
                    summary.MeshTypeLabel = LabelMeshType(meshTypeInt);
                }
                catch (Exception ex) { Warn(summary, "Mesh type label: " + ex.Message); }

                // Mesher used — string label like "Curvature-based mesh"
                try
                {
                    int mesherInt = (int)((dynamic)study).Mesher;
                    summary.MesherUsed = LabelMesher(mesherInt);
                }
                catch (Exception ex) { Warn(summary, "Mesher kind: " + ex.Message); }

                // Element sizes (m → mm)
                try
                {
                    double maxSizeM = (double)((dynamic)mesh).ElementSize;
                    if (maxSizeM > 0) summary.MaxElementSizeMm = maxSizeM * 1000.0;
                }
                catch (Exception ex) { Warn(summary, "Mesh max element size: " + ex.Message); }

                try
                {
                    double minSizeM = (double)((dynamic)mesh).MinElementSize;
                    if (minSizeM > 0) summary.MinElementSizeMm = minSizeM * 1000.0;
                }
                catch (Exception ex) { Warn(summary, "Mesh min element size: " + ex.Message); }

                // Jacobian points (typically 4, 8, 16, 29 — high-quality default is 16)
                try
                {
                    int jp = (int)((dynamic)mesh).JacobianPoints;
                    if (jp > 0) summary.JacobianPoints = jp;
                }
                catch (Exception ex) { Warn(summary, "Mesh Jacobian points: " + ex.Message); }

                // Quality label / aspect ratio statistics — not reliably exposed through ICWMesh.
                // Leave null and warn so the gap is visible.
                Warn(summary, "Mesh quality label and aspect-ratio statistics are not exposed via the COSMOSWorks API; left null.");
            }
            catch (Exception ex)
            {
                Warn(summary, "Mesh extras extraction failed: " + ex.Message);
            }
            finally
            {
                SafeReleaseCom(mesh);
            }
        }

        /// <summary>
        /// Iterate study.LoadsAndRestraintsManager and conservatively split verified
        /// load categories from restraint categories. Raw type integers are always
        /// preserved; unverified categories are omitted with warnings.
        /// </summary>
        private void ExtractLoadsAndFixtures(dynamic study, FeaResultsSummary summary)
        {
            object lrMgrObj = null;
            try
            {
                try { lrMgrObj = ((dynamic)study).LoadsAndRestraintsManager; }
                catch (Exception ex) { Warn(summary, "LoadsAndRestraintsManager not accessible: " + ex.Message); return; }

                if (lrMgrObj == null) { Warn(summary, "LoadsAndRestraintsManager is null on this study."); return; }

                int count = 0;
                try { count = (int)((dynamic)lrMgrObj).Count; }
                catch (Exception ex) { Warn(summary, "Could not read loads/restraints count: " + ex.Message); return; }

                for (int i = 0; i < count; i++)
                {
                    object item = null;
                    try
                    {
                        int err = 0;
                        item = ((dynamic)lrMgrObj).GetLoadsAndRestraints(i, out err);
                        if (item == null) continue;

                        string name = null;
                        try { name = ((dynamic)item).Name as string; } catch { }

                        int? typeRaw = null;
                        try { typeRaw = (int)((dynamic)item).Type; } catch { }

                        int? entityCount = null;
                        try { entityCount = (int)((dynamic)item).EntityCount; } catch { }

                        if (!typeRaw.HasValue)
                        {
                            Warn(summary, $"Loads/restraints item {i} ('{name ?? "(unnamed)"}') did not expose a raw Type integer; category left unreported.");
                            continue;
                        }

                        string typeLabel = LabelLoadsAndRestraintsType(typeRaw.Value);
                        if (IsFixtureCategory(typeRaw.Value))
                        {
                            int? subtypeRaw = TryReadIntProperty(item, "RestraintType");
                            summary.Fixtures.Add(new FixtureInfo
                            {
                                Name = name,
                                Type = typeLabel,
                                TypeRaw = typeRaw,
                                Subtype = subtypeRaw.HasValue ? LabelRestraintSubtype(subtypeRaw.Value) : null,
                                SubtypeRaw = subtypeRaw,
                                EntityCount = entityCount,
                                EntityKind = null
                            });
                            if (!subtypeRaw.HasValue)
                                Warn(summary, $"Fixture '{name ?? "(unnamed)"}' type_raw={typeRaw.Value} did not expose a verified restraint subtype; subtype left null.");
                        }
                        else if (IsLoadCategory(typeRaw.Value))
                        {
                            int? subtypeRaw = TryReadLoadSubtype(item, typeRaw.Value);
                            double? value = TryReadLoadValue(item, typeRaw.Value);
                            string unit = value.HasValue ? "raw" : null;

                            summary.Loads.Add(new LoadInfo
                            {
                                Name = name,
                                Type = typeLabel,
                                TypeRaw = typeRaw,
                                Subtype = subtypeRaw.HasValue ? LabelLoadSubtype(typeRaw.Value, subtypeRaw.Value) : null,
                                SubtypeRaw = subtypeRaw,
                                Value = value,
                                ValueUnit = unit,
                                EntityCount = entityCount,
                                EntityKind = null
                            });
                        }
                        else
                        {
                            Warn(summary, $"Loads/restraints item {i} ('{name ?? "(unnamed)"}') has unclassified type_raw={typeRaw.Value}; not placing it in loads[] or fixtures[].");
                        }
                    }
                    catch (Exception ex)
                    {
                        Warn(summary, $"Loads/restraints item {i} extraction failed: {ex.Message}");
                    }
                    finally
                    {
                        SafeReleaseCom(item);
                    }
                }

                if (summary.Loads.Count == 0 && summary.Fixtures.Count == 0)
                {
                    Warn(summary, "LoadsAndRestraintsManager returned no confidently categorized loads/fixtures; arrays left empty.");
                }
                else
                {
                    Warn(summary, "Load/fixture entity_kind (face/edge/vertex) is not exposed via the COSMOSWorks API; left null on every categorized item.");
                }
            }
            finally
            {
                SafeReleaseCom(lrMgrObj);
            }
        }

        /// <summary>
        /// Best-effort extraction of equivalent strain (ESTRN) extremes.
        /// Wrapped because the strain API surface and component enum differ across versions.
        /// </summary>
        private void ExtractStrainExtremes(ICWResults results, FeaResultsSummary summary)
        {
            try
            {
                int errCode = 0;
                // ESTRN is typically component 17 in swsStrainComponent_e — try and fall through.
                // GetMinMaxStrain(NComponent, NElementNumber, NStepNum, DispPlane, NUnits, out ErrorCode)
                object strainResult = ((dynamic)results).GetMinMaxStrain(
                    17,        // ESTRN heuristic — wrapped, falls through on error
                    0,         // all elements
                    0,         // step
                    null,      // ref plane
                    0,         // units
                    out errCode);
                if (errCode == 0 && strainResult != null)
                {
                    double[] vals = strainResult as double[];
                    if (vals != null && vals.Length >= 2)
                    {
                        summary.MaxStrain = vals[1];
                    }
                    else
                    {
                        Warn(summary, "GetMinMaxStrain returned an unexpected payload shape; max strain left null.");
                    }
                }
                else
                {
                    Warn(summary, $"GetMinMaxStrain returned error code {errCode}; max strain left null.");
                }
            }
            catch (Exception ex)
            {
                Warn(summary, "GetMinMaxStrain not available on this version: " + ex.Message);
            }

            // GetMinMaxStrain returns scalar extremes only — there is no clean
            // per-node strain accessor analogous to GetTranslationalDisplacement,
            // so MaxStrainNode is unconditionally null in the JSON. Document the gap.
            Warn(summary, "Per-node strain (max_strain_node) is not exposed via the COSMOSWorks API; left null.");
        }

