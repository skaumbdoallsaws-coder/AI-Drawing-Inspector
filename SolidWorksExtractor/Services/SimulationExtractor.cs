        /// <summary>
        /// Build a deterministic, filesystem-safe slug from the study name.
        /// Properties:
        ///   - Pure function of (studyName, indexFallback) — same input always yields the same slug.
        ///   - Lowercases ASCII to dodge case-sensitivity differences across filesystems.
        ///   - Hyphens are preserved (common in engineering study names like "Static-1").
        ///   - All other non-alphanumeric chars become underscores; runs are collapsed.
        ///   - Capped at 64 chars; trimmed of leading/trailing _ and -.
        ///   - Falls back to "study_{index}" or "study_unknown" if sanitisation empties the slug.
        /// </summary>
        private static string MakeStudySlug(string studyName, int indexFallback)
        {
            if (string.IsNullOrWhiteSpace(studyName))
            {
                return indexFallback >= 0 ? $"study_{indexFallback}" : "study_unknown";
            }

            var sb = new StringBuilder(studyName.Length);
            foreach (char c in studyName)
            {
                if (c < 128 && char.IsLetterOrDigit(c))
                    sb.Append(char.ToLowerInvariant(c));
                else if (c == '-')
                    sb.Append('-');
                else
                    sb.Append('_');
            }

            string s = sb.ToString();
            // Collapse runs of underscores
            while (s.Contains("__")) s = s.Replace("__", "_");
            s = s.Trim('_', '-');

            const int maxLen = 64;
            if (s.Length > maxLen) s = s.Substring(0, maxLen).TrimEnd('_', '-');

            if (string.IsNullOrEmpty(s))
                return indexFallback >= 0 ? $"study_{indexFallback}" : "study_unknown";
            return s;
        }

        /// <summary>
        /// Resolve the 0-based index of a study by its name (case-insensitive). Returns -1 if not found.
        /// Used to record the resolved index in the manifest when the user picked by name.
        /// </summary>
        private int ResolveStudyIndex(dynamic studyMgr, int studyCount, string studyName)
        {
            if (string.IsNullOrWhiteSpace(studyName)) return -1;
            for (int i = 0; i < studyCount; i++)
            {
                dynamic candidate = null;
                try
                {
                    candidate = studyMgr.GetStudy(i);
                    if (candidate == null) continue;
                    string n = null;
                    try { n = candidate.Name as string; } catch { }
                    if (!string.IsNullOrEmpty(n)
                        && string.Equals(n, studyName, StringComparison.OrdinalIgnoreCase))
                    {
                        return i;
                    }
                }
                catch { }
                finally
                {
                    SafeReleaseCom(candidate);
                }
            }
            return -1;
        }

        /// <summary>
        /// Best-effort lookup of the current git commit, by invoking `git rev-parse HEAD`
        /// from the executable's directory. The repo's .git is found by git's normal
        /// upward walk. Returns null on any failure (no git, not a repo, timeout).
        /// </summary>
        private static string TryGetGitCommit()
        {
            try
            {
                string cwd = AppDomain.CurrentDomain.BaseDirectory ?? System.Environment.CurrentDirectory;
                var psi = new System.Diagnostics.ProcessStartInfo("git", "rev-parse HEAD")
                {
                    WorkingDirectory = cwd,
                    UseShellExecute = false,
                    RedirectStandardOutput = true,
                    RedirectStandardError = true,
                    CreateNoWindow = true
                };
                using (var p = System.Diagnostics.Process.Start(psi))
                {
                    if (p == null) return null;
                    string outp = p.StandardOutput.ReadToEnd();
                    if (!p.WaitForExit(2000))
                    {
                        try { p.Kill(); } catch { }
                        return null;
                    }
                    if (p.ExitCode != 0) return null;
                    string sha = (outp ?? "").Trim();
                    return string.IsNullOrEmpty(sha) ? null : sha;
                }
            }
            catch
            {
                return null;
            }
        }

