        /// <summary>
        /// Lightweight description of a single Simulation study, returned by ListStudies()
        /// for preflight reporting and explicit study selection.
        /// </summary>
        public class StudyInfo
        {
            public int Index;            // 0-based index in StudyManager
            public string Name;          // Display name (null if unreadable)
            public int AnalysisType;     // swsAnalysisStudyType_e value (0 = static)
            public string AnalysisTypeLabel; // "static", "frequency", "thermal", ... or "unknown(N)"
            public bool HasResults;      // True if Results object is accessible (results computed)
            public string Note;          // Optional diagnostic note (e.g. "results error: ...")
        }

        /// <summary>
        /// Enumerate every study in the active part document.
        /// Safe to call without picking one. Returns an empty list if Simulation
        /// add-in is unavailable or no document is open.
        /// </summary>
        public List<StudyInfo> ListStudies(ISldWorks swApp, IModelDoc2 modelDoc)
        {
            var result = new List<StudyInfo>();
            if (swApp == null || modelDoc == null)
                return result;

            object cosAddinObj = null;
            ICosmosWorks cosWorks = null;
            dynamic studyMgr = null;

            try
            {
                cosAddinObj = swApp.GetAddInObject("SldWorks.Simulation");
                if (cosAddinObj == null) return result;

                cosWorks = (ICosmosWorks)((dynamic)cosAddinObj).CosmosWorks;
                if (cosWorks == null) return result;

                CWModelDoc cwDoc = (CWModelDoc)cosWorks.ActiveDoc;
                if (cwDoc == null) return result;

                studyMgr = ((dynamic)cwDoc).StudyManager;
                if (studyMgr == null) return result;

                int studyCount = studyMgr.StudyCount;
                for (int i = 0; i < studyCount; i++)
                {
                    dynamic candidate = null;
                    var info = new StudyInfo { Index = i };
                    try
                    {
                        candidate = studyMgr.GetStudy(i);
                        if (candidate == null)
                        {
                            info.Note = "GetStudy returned null";
                            result.Add(info);
                            continue;
                        }

                        try { info.Name = candidate.Name as string; }
                        catch { info.Name = null; }

                        try { info.AnalysisType = (int)candidate.AnalysisType; }
                        catch { info.AnalysisType = -1; }

                        info.AnalysisTypeLabel = LabelAnalysisType(info.AnalysisType);

                        string resultsNote;
                        info.HasResults = EnsureResultsAvailable(candidate, modelDoc, out resultsNote);
                        if (!string.IsNullOrWhiteSpace(resultsNote))
                            info.Note = resultsNote;
                    }
                    catch (Exception ex)
                    {
                        info.Note = "study error: " + ex.Message;
                    }
                    finally
                    {
                        SafeReleaseCom(candidate);
                    }

                    result.Add(info);
                }
            }
            catch (Exception ex)
            {
                Console.WriteLine($"    FEA: ListStudies failed: {ex.Message}");
            }
            finally
            {
                SafeReleaseCom(studyMgr);
                SafeReleaseCom(cosWorks);
                SafeReleaseCom(cosAddinObj);
            }

            return result;
        }

        /// <summary>
        /// Print a preflight report: SolidWorks version, Simulation add-in availability,
        /// active document path, and a table of all studies in the document.
        /// Returns true if the Simulation add-in is reachable, false otherwise.
        /// </summary>
        public bool RunPreflight(ISldWorks swApp, IModelDoc2 modelDoc)
        {
            Console.WriteLine();
            Console.WriteLine("─── FEA Preflight ───");

            // SolidWorks version
            string swVersion = "unknown";
            try
            {
                if (swApp != null)
                    swVersion = swApp.RevisionNumber() as string ?? "unknown";
            }
            catch { }
            Console.WriteLine($"  SolidWorks version : {swVersion}");

            // Document path
            string docPath = "(no document)";
            try
            {
                if (modelDoc != null)
                {
                    string p = modelDoc.GetPathName();
                    docPath = string.IsNullOrWhiteSpace(p) ? "(unsaved document)" : p;
                }
            }
            catch { }
            Console.WriteLine($"  Document path      : {docPath}");

            // Add-in reachability
            bool addinReachable = false;
            object cosAddinObj = null;
            try
            {
                if (swApp != null)
                {
                    cosAddinObj = swApp.GetAddInObject("SldWorks.Simulation");
                    addinReachable = cosAddinObj != null;
                }
            }
            catch { }
            finally
            {
                SafeReleaseCom(cosAddinObj);
            }
            Console.WriteLine($"  Simulation add-in  : {(addinReachable ? "available" : "NOT available — license/add-in not active")}");

            // Studies
            var studies = ListStudies(swApp, modelDoc);
            Console.WriteLine($"  Studies found      : {studies.Count}");
            if (studies.Count > 0)
            {
                Console.WriteLine("  Index | Name                                   | Type      | Results");
                Console.WriteLine("  ------+----------------------------------------+-----------+--------");
                foreach (var s in studies)
                {
                    string name = (s.Name ?? "(unnamed)");
                    if (name.Length > 38) name = name.Substring(0, 35) + "...";
                    string type = s.AnalysisTypeLabel ?? "unknown";
                    if (type.Length > 9) type = type.Substring(0, 9);
                    string hr = s.HasResults ? "yes" : "no";
                    Console.WriteLine($"  {s.Index,5} | {name,-38} | {type,-9} | {hr}");
                    if (!string.IsNullOrEmpty(s.Note))
                        Console.WriteLine($"        Note: {s.Note}");
                }
            }
            Console.WriteLine("─── End preflight ───");
            Console.WriteLine();

            return addinReachable;
        }

        private static string LabelAnalysisType(int t)
        {
            // swsAnalysisStudyType_e values (commonly used)
            switch (t)
            {
                case 0: return "static";
                case 1: return "frequency";
                case 2: return "buckling";
                case 3: return "thermal";
                case 4: return "drop";
                case 5: return "fatigue";
                case 6: return "optimize";
                case 7: return "nonlinear";
                case 8: return "linDyn";
                case 9: return "pressure";
                case 10: return "design";
                case 11: return "submodel";
                default: return $"unknown({t})";
            }
        }

