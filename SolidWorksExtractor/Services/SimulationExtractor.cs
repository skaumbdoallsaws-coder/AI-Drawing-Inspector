            }
            sb.AppendLine("  ],");

            // --- fixtures array ---
            sb.AppendLine("  \"fixtures\": [");
            for (int i = 0; i < summary.Fixtures.Count; i++)
            {
                var F = summary.Fixtures[i];
                sb.Append("    {");
                sb.Append("\"name\":").Append(JsonStringOrNull(F.Name));
                sb.Append(",\"type\":").Append(JsonStringOrNull(F.Type));
                sb.Append(",\"type_raw\":").Append(JsonIntOrNull(F.TypeRaw));
                sb.Append(",\"subtype\":").Append(JsonStringOrNull(F.Subtype));
                sb.Append(",\"subtype_raw\":").Append(JsonIntOrNull(F.SubtypeRaw));
                sb.Append(",\"entity_count\":").Append(JsonIntOrNull(F.EntityCount));
                sb.Append(",\"entity_kind\":").Append(JsonStringOrNull(F.EntityKind));
                sb.Append("}");
                if (i < summary.Fixtures.Count - 1) sb.Append(",");
                sb.AppendLine();
            }
            sb.AppendLine("  ],");

            // --- results detail ---
            sb.AppendLine("  \"results\": {");
            sb.AppendFormat(CultureInfo.InvariantCulture, "    \"min_von_mises_mpa\": {0:G6},\n", summary.MinVonMisesMpa);
            sb.AppendFormat(CultureInfo.InvariantCulture, "    \"max_von_mises_mpa\": {0:G6},\n", summary.MaxVonMisesMpa);
            sb.Append("    \"max_von_mises_node\": ").Append(summary.MaxStressNode > 0 ? summary.MaxStressNode.ToString(CultureInfo.InvariantCulture) : "null").AppendLine(",");
            sb.AppendFormat(CultureInfo.InvariantCulture,
                "    \"max_von_mises_location_m\": [{0:G9}, {1:G9}, {2:G9}],\n",
                summary.MaxStressLocation[0], summary.MaxStressLocation[1], summary.MaxStressLocation[2]);
            sb.AppendFormat(CultureInfo.InvariantCulture, "    \"max_displacement_mm\": {0:G6},\n", summary.MaxDisplacementMm);
            sb.Append("    \"max_displacement_node\": ").Append(summary.MaxDisplacementNode > 0 ? summary.MaxDisplacementNode.ToString(CultureInfo.InvariantCulture) : "null").AppendLine(",");
            sb.AppendFormat(CultureInfo.InvariantCulture, "    \"max_strain\": {0},\n", JsonNumberOrNull(summary.MaxStrain, "G6"));
            sb.Append("    \"max_strain_node\": ").Append(summary.MaxStrainNode > 0 ? summary.MaxStrainNode.ToString(CultureInfo.InvariantCulture) : "null").AppendLine(",");
            sb.AppendFormat(CultureInfo.InvariantCulture,
                "    \"reaction_forces_n\": {{ \"fx\": {0:G6}, \"fy\": {1:G6}, \"fz\": {2:G6} }},\n",
                summary.ReactionFx, summary.ReactionFy, summary.ReactionFz);
            if (summary.HasReactionMoments)
            {
                sb.AppendFormat(CultureInfo.InvariantCulture,
                    "    \"reaction_moments_nm\": {{ \"mx\": {0:G6}, \"my\": {1:G6}, \"mz\": {2:G6} }},\n",
                    summary.ReactionMx, summary.ReactionMy, summary.ReactionMz);
            }
            else
            {
                sb.AppendLine("    \"reaction_moments_nm\": null,");
            }
            sb.AppendFormat(CultureInfo.InvariantCulture, "    \"safety_factor\": {0:F2},\n", summary.SafetyFactor);
            sb.AppendFormat(CultureInfo.InvariantCulture, "    \"morph_target_available\": {0}\n", summary.HasMorphTarget ? "true" : "false");
            sb.AppendLine("  },");

            // --- warnings array (mirror of manifest warnings) ---
            sb.AppendLine("  \"warnings\": [");
            if (summary.Warnings != null)
            {
                for (int i = 0; i < summary.Warnings.Count; i++)
                {
                    sb.Append("    \"").Append(EscapeJson(summary.Warnings[i])).Append("\"");
                    if (i < summary.Warnings.Count - 1) sb.Append(",");
                    sb.AppendLine();
                }
            }
            sb.AppendLine("  ]");

            sb.Append("}");

            File.WriteAllText(outputPath, sb.ToString(), Encoding.UTF8);
        }

        // --- JSON formatting helpers (write null for missing data, never a placeholder) ---

        private static string JsonStringOrNull(string s)
        {
            if (s == null) return "null";
            return "\"" + EscapeJsonStatic(s) + "\"";
        }

        private static string JsonIntOrNull(int? v)
        {
            if (!v.HasValue) return "null";
            return v.Value.ToString(CultureInfo.InvariantCulture);
        }

        private static string JsonNumberOrNull(double? v, string format)
        {
            if (!v.HasValue || double.IsNaN(v.Value) || double.IsInfinity(v.Value)) return "null";
            return v.Value.ToString(format, CultureInfo.InvariantCulture);
        }

        private static string EscapeJsonStatic(string s)
        {
            if (string.IsNullOrEmpty(s)) return "";
            return s.Replace("\\", "\\\\")
                    .Replace("\"", "\\\"")
                    .Replace("\n", "\\n")
                    .Replace("\r", "\\r")
                    .Replace("\t", "\\t");
        }

